import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-balanced/html/d3d833b1-fa73-11f0-83e0-8d7be1d51901.html';

// Page Object for the Two Pointers app
class TwoPointersPage {
  constructor(page) {
    this.page = page;
    this.problemSelect = page.locator('#problemSelect');
    this.arrayInput = page.locator('#arrayInput');
    this.targetInput = page.locator('#targetInput');
    this.targetBox = page.locator('#targetBox');
    this.resetBtn = page.locator('#resetBtn');
    this.prevBtn = page.locator('#prevBtn');
    this.playBtn = page.locator('#playBtn');
    this.nextBtn = page.locator('#nextBtn');
    this.visualArea = page.locator('#visualArea');
    this.codeBox = page.locator('#codeBox');
    this.explainBox = page.locator('#explainBox');
    this.stateDetails = page.locator('#stateDetails');
    this.resultBox = page.locator('#resultBox');
    this.stepStatus = page.locator('#stepStatus');
    this.speed = page.locator('#speed');
  }

  async goto() {
    await this.page.goto(APP_URL);
  }

  async clickReset() {
    await this.resetBtn.click();
  }

  async clickPrev() {
    await this.prevBtn.click();
  }

  async clickPlay() {
    await this.playBtn.click();
  }

  async clickNext() {
    await this.nextBtn.click();
  }

  async selectProblem(value) {
    await this.problemSelect.selectOption(value);
    // trigger change handler (selectOption triggers change)
  }

  async setArray(value) {
    await this.arrayInput.fill(value);
  }

  async setTarget(value) {
    await this.targetInput.fill(String(value));
  }

  async setSpeed(value) {
    await this.speed.fill(String(value));
  }

  async getStepStatusText() {
    return (await this.stepStatus.textContent())?.trim();
  }

  async getPlayButtonText() {
    return (await this.playBtn.textContent())?.trim();
  }

  async visualHasCells() {
    return await this.visualArea.locator('.array-row .cell').count() > 0;
  }

  async visualCells() {
    return this.visualArea.locator('.array-row .cell');
  }

  async visualHasBars() {
    return await this.visualArea.locator('.bars .bar').count() > 0;
  }

  async getCodeText() {
    return (await this.codeBox.textContent()) || '';
  }

  async getStateDetails() {
    return (await this.stateDetails.textContent()) || '';
  }

  async getResultText() {
    return (await this.resultBox.textContent()) || '';
  }
}

test.describe('Two Pointers — Interactive Demo (FSM tests)', () => {
  // capture console messages and page errors for assertions
  let consoleMessages;
  let pageErrors;

  test.beforeEach(async ({ page }) => {
    consoleMessages = [];
    pageErrors = [];

    page.on('console', (msg) => {
      consoleMessages.push({ type: msg.type(), text: msg.text() });
    });

    page.on('pageerror', (err) => {
      pageErrors.push(err);
    });

    // Accept any alert/dialog automatically to allow flows that trigger alert()
    page.on('dialog', async (dialog) => {
      await dialog.accept();
    });
  });

  test('Initialization: page loads and generates initial steps (S1_StepsGenerated)', async ({ page }) => {
    // Validate initial load and that steps are generated by the app's initialization
    const app = new TwoPointersPage(page);
    await app.goto();

    // The app's script dispatches change and generateSteps on load; ensure step status reflects generated steps
    const status = await app.getStepStatusText();
    expect(status).toMatch(/Step \d+ \/ \d+/);

    // Code box should contain pseudocode for the selected problem (default twoSum)
    const code = await app.getCodeText();
    expect(code).toContain('left = 0');
    expect(code).toContain('right = n - 1');

    // Visual should render array cells for twoSum (array-row .cell)
    expect(await app.visualHasCells()).toBe(true);

    // State details should include left/right and array representation
    const details = await app.getStateDetails();
    expect(details).toMatch(/left: \d+/);
    expect(details).toMatch(/right: \d+/);
    expect(details).toMatch(/array:/);

    // Ensure there were no uncaught page errors during initialization
    expect(pageErrors.length).toBe(0);
    // Expect no console.error messages
    const errors = consoleMessages.filter(m => m.type === 'error');
    expect(errors.length).toBe(0);
  });

  test('Idle state (S0_Idle) when generating with empty array: shows "No steps to display."', async ({ page }) => {
    // This test forces the app into a "no steps" state by providing an empty array and clicking Reset.
    const app = new TwoPointersPage(page);
    await app.goto();

    // Clear array input and click reset; app will alert and not generate steps
    await app.setArray('');
    await app.clickReset();

    // After alert accepted, the UI should display the "No steps to display." message in visualArea
    const visualHtml = await app.visualArea.innerHTML();
    expect(visualHtml).toContain('No steps to display.');

    // Step status should be 0 / 0 indicating Idle-like state
    const status = await app.getStepStatusText();
    expect(status).toMatch(/Step 0 \/ 0/);

    // code box should be empty and explanation says to generate steps
    const code = await app.getCodeText();
    expect(code.trim()).toBe('');

    const explain = await app.explainBox.textContent();
    expect(explain).toContain('Generate steps');

    // No page errors expected
    expect(pageErrors.length).toBe(0);
  });

  test('Generate steps and navigate Next/Prev (S1_StepsGenerated transitions)', async ({ page }) => {
    // Validate generateSteps transition and Next/Prev transitions while paused
    const app = new TwoPointersPage(page);
    await app.goto();

    // Ensure we start from a known state; set to twoSum example and regenerate
    await app.selectProblem('twoSum');
    await app.setArray('1,2,3,4,6,8');
    await app.setTarget(10);

    await app.clickReset();

    // After generation, step status should show > 0 total steps
    let status = await app.getStepStatusText();
    expect(status).toMatch(/Step \d+ \/ \d+/);
    const match = status.match(/Step (\d+) \/ (\d+)/);
    expect(match).not.toBeNull();
    const current = Number(match[1]);
    const total = Number(match[2]);
    expect(total).toBeGreaterThan(0);
    expect(current).toBeGreaterThanOrEqual(1);

    // Record current details
    const beforeDetails = await app.getStateDetails();

    // Click Next: should pause and render the next step (index + 1)
    await app.clickNext();
    const afterNextStatus = await app.getStepStatusText();
    expect(afterNextStatus).not.toBe(status); // should change

    // Click Prev: should go back (or to previous index)
    await app.clickPrev();
    const afterPrevStatus = await app.getStepStatusText();
    // Because of bounds, afterPrevStatus might equal original or earlier; assert it's a string with Step
    expect(afterPrevStatus).toMatch(/Step \d+ \/ \d+/);

    // Visual should still render cells
    expect(await app.visualHasCells()).toBe(true);

    // No page errors occurred
    expect(pageErrors.length).toBe(0);
  });

  test('Play / Pause transitions (S1 -> S2_Playing -> S3_Paused and back)', async ({ page }) => {
    // Validate play starts animation and advances step index, pause stops it, and play resumes
    const app = new TwoPointersPage(page);
    await app.goto();

    // Make the animation faster to keep tests quick
    await app.setSpeed('200'); // set to minimum speed to see quick advances

    // Regenerate steps to ensure known data
    await app.selectProblem('twoSum');
    await app.setArray('1,2,3,4,6,8');
    await app.setTarget(10);
    await app.clickReset();

    // Initial play button should show Play
    let playText = await app.getPlayButtonText();
    expect(playText).toMatch(/Play/i);

    // Click Play — enters Playing (S2_Playing): button becomes "Pause"
    await app.clickPlay();
    // immediately check button label toggled to Pause
    playText = await app.getPlayButtonText();
    expect(playText).toMatch(/Pause/i);

    // Wait briefly for the animation to advance at least one step
    const initialStatus = await app.getStepStatusText();
    // Wait up to 2 seconds for step to advance (speed ~200ms)
    await page.waitForTimeout(700);
    const statusAfterPlay = await app.getStepStatusText();
    expect(statusAfterPlay).not.toBe(initialStatus);

    // Click Play again to pause (S2 -> S3)
    await app.clickPlay();
    const pausedText = await app.getPlayButtonText();
    expect(pausedText).toMatch(/Play/i);

    // Ensure no page errors
    expect(pageErrors.length).toBe(0);

    // Resume playing to verify toggling back to Playing
    await app.clickPlay();
    expect(await app.getPlayButtonText()).toMatch(/Pause/i);
    // Pause again for cleanup
    await app.clickPlay();
    expect(await app.getPlayButtonText()).toMatch(/Play/i);
  });

  test('Problem selection changes (PROBLEM_SELECT_CHANGE) and rendering differences', async ({ page }) => {
    // Validate selecting different problems updates UI (targetBox visibility), example arrays, and rendering mode
    const app = new TwoPointersPage(page);
    await app.goto();

    // Select containerWater: target box should be hidden and visual should render bars after generating
    await app.selectProblem('containerWater');
    // targetBox.style.display should be 'none'
    const targetDisplay = await app.targetBox.evaluate(el => getComputedStyle(el).display);
    expect(targetDisplay === 'none' || targetDisplay === 'block').toBeTruthy(); // it may be 'none' as intended
    // Click reset to generate steps for containerWater
    await app.clickReset();

    // Visual should show bars
    expect(await app.visualHasBars()).toBe(true);

    // Code box should contain "maxArea" pseudocode for containerWater
    const code = await app.getCodeText();
    expect(code).toContain('maxArea');

    // Select removeDup and generate steps, then verify result text contains Unique count at done
    await app.selectProblem('removeDup');
    // set a sample sorted array (already example changed by select, but set explicitly)
    await app.setArray('0,0,1,1,1,2,2,3,3,4');
    await app.clickReset();

    // Navigate to the final step by clicking Next until status shows last step
    let status = await app.getStepStatusText();
    const totalMatch = status.match(/Step \d+ \/ (\d+)/);
    let total = totalMatch ? Number(totalMatch[1]) : 0;
    // Click Next (total-1) times to reach the last step (already on 1)
    for (let i = 0; i < total + 2; i++) {
      // safeguard: click Next repeatedly; the app clamps stepIndex inside renderStep
      await app.clickNext();
      await page.waitForTimeout(50);
    }
    // Result box at done should mention "Unique count" (renderResultText)
    const resultText = await app.getResultText();
    expect(resultText.toLowerCase()).toContain('unique');

    // No page errors
    expect(pageErrors.length).toBe(0);
  });

  test('Edge cases: non-numeric input produces alert and prevents generation (invalid inputs)', async ({ page }) => {
    // Test alerts for invalid inputs for numeric-only problems (twoSum & removeDup)
    const app = new TwoPointersPage(page);
    await app.goto();

    // Spy on dialogs: beforeEach already auto-accepts, but we can track console messages for robustness.
    // Set non-numeric array for twoSum and attempt to generate -> app should alert and not produce steps
    await app.selectProblem('twoSum');
    await app.setArray('a,b,c');
    await app.clickReset();

    // After invalid input, the visual should likely still contain the previous or no steps;
    // But importantly, no uncaught exceptions should have occurred
    expect(pageErrors.length).toBe(0);

    // Now test removeDup non-numeric input
    await app.selectProblem('removeDup');
    await app.setArray('x,y,z');
    await app.clickReset();
    // Again, ensure no uncaught exceptions in page runtime
    expect(pageErrors.length).toBe(0);
  });

  test('Keyboard shortcuts: Space toggles play/pause, ArrowRight / ArrowLeft navigate steps', async ({ page }) => {
    // Validate that keyboard shortcuts wired by the app behave as expected
    const app = new TwoPointersPage(page);
    await app.goto();

    // Ensure there are generated steps
    await app.clickReset();
    const initialStatus = await app.getStepStatusText();

    // Press Space to toggle play; should switch to Pause label
    await page.keyboard.press('Space');
    await page.waitForTimeout(100); // allow handler to run
    let playText = await app.getPlayButtonText();
    // After pressing space, playback toggles; ensure label changed
    expect(['Pause', 'Play']).toContain(playText);

    // If playing, pause it so test can continue deterministically
    if (playText === 'Pause') {
      await page.keyboard.press('Space');
      await page.waitForTimeout(50);
      expect(await app.getPlayButtonText()).toBe('Play');
    }

    // Press ArrowRight to go to next step (should pause and render next)
    await page.keyboard.press('ArrowRight');
    await page.waitForTimeout(50);
    const statusAfterRight = await app.getStepStatusText();
    expect(statusAfterRight).toMatch(/Step \d+ \/ \d+/);
    expect(statusAfterRight).not.toBeNull();

    // Press ArrowLeft to go to previous step
    await page.keyboard.press('ArrowLeft');
    await page.waitForTimeout(50);
    const statusAfterLeft = await app.getStepStatusText();
    expect(statusAfterLeft).toMatch(/Step \d+ \/ \d+/);

    // No uncaught page errors
    expect(pageErrors.length).toBe(0);
  });

  test.afterEach(async ({ page }) => {
    // Final assertion: ensure no unexpected console.error or uncaught page errors across tests
    const errorConsoleMsgs = consoleMessages.filter(m => m.type === 'error');
    expect(errorConsoleMsgs.length).toBe(0);
    expect(pageErrors.length).toBe(0);
  });
});