import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-balanced/html/63b40d62-fa74-11f0-bb9a-db7e6ecdeeaa.html';

// Page object for the demo page
class DemoPage {
  /**
   * @param {import('@playwright/test').Page} page
   */
  constructor(page) {
    this.page = page;
    this.runBtn = page.locator('#runBtn');
    this.output = page.locator('#output');
  }

  async goto() {
    await this.page.goto(APP_URL);
  }

  async clickRun(options) {
    await this.runBtn.click(options);
  }

  async outputText() {
    return (await this.output.textContent()) ?? '';
  }

  async outputInnerText() {
    return (await this.output.innerText()) ?? '';
  }

  async hasOutputClass() {
    return await this.output.evaluate((el) => el.classList.contains('output'));
  }
}

test.describe('Dynamic Typing Demo (FSM: Idle -> Demo Running)', () => {
  let demo;
  let consoleMessages;
  let pageErrors;

  // Expected output exactly as generated by the page's runDemo() function
  const expectedOutput =
    "Initial: number : 42\n" +
    "Assigned string: string : Now I'm a string!\n" +
    "Assigned boolean: boolean : true\n" +
    "Assigned object: object : {\"message\":\"I'm an object now\"}\n";

  // Attach listeners and navigate to the page before each test
  test.beforeEach(async ({ page }) => {
    demo = new DemoPage(page);
    consoleMessages = [];
    pageErrors = [];

    // Capture console messages for inspection (type + text)
    page.on('console', (msg) => {
      consoleMessages.push({ type: msg.type(), text: msg.text() });
    });

    // Capture page errors (uncaught exceptions)
    page.on('pageerror', (err) => {
      pageErrors.push(err);
    });

    await demo.goto();
  });

  // Clean up listeners implicitly by Playwright fixture lifecycle

  test('S0 Idle: initial render shows Run Demo button and empty output', async ({ page }) => {
    // Validate that the page rendered (this corresponds to the S0 entry action "renderPage()")
    await expect(page.locator('h1')).toHaveText(/JavaScript Dynamic Typing Demonstration/i);

    // The Run Demo button should be visible and enabled
    await expect(demo.runBtn).toBeVisible();
    await expect(demo.runBtn).toBeEnabled();
    await expect(demo.runBtn).toHaveText('Run Demo');

    // The output div should exist and be initially empty
    const initialOutput = await demo.outputText();
    expect(initialOutput).toBe('', 'Output should be empty on initial render (Idle state)');

    // The output element should have the "output" class (DOM structure/evidence)
    expect(await demo.hasOutputClass()).toBe(true);

    // Assert there were no uncaught runtime page errors during initial render
    expect(pageErrors.length).toBe(0);
    // Assert there were no console errors emitted during initial render
    const consoleErrors = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrors.length).toBe(0);
  });

  test('Transition (RunDemo_Click): clicking "Run Demo" enters Demo Running and updates output', async () => {
    // Click the Run Demo button to trigger the transition S0 -> S1 (RunDemo_Click)
    await demo.clickRun();

    // Wait for the output to be populated (runDemo clears and writes output)
    await expect(demo.output).toHaveText(/Initial:/);

    // Validate entire expected output text content (exact match, including newlines)
    const actualOutput = await demo.outputText();
    expect(actualOutput).toBe(expectedOutput, 'Output should list the variable types and values in order');

    // Confirm that runDemo executed (S1 entry action) by observing the DOM change
    // (The presence of the expected lines demonstrates runDemo ran)
    expect(actualOutput.startsWith('Initial: number : 42')).toBe(true);

    // No uncaught page errors should have occurred during the demo run
    expect(pageErrors.length).toBe(0);

    // No console errors were emitted during the run
    const consoleErrors = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrors.length).toBe(0);
  });

  test('Clicking Run Demo twice clears previous output before re-writing (idempotent runDemo)', async () => {
    // First click to produce initial output
    await demo.clickRun();
    await expect(demo.output).toHaveText(/Initial:/);
    const firstOutput = await demo.outputText();
    expect(firstOutput).toBe(expectedOutput);

    // Second click should clear previous output and then write the same content again
    await demo.clickRun();
    await expect(demo.output).toHaveText(/Initial:/);
    const secondOutput = await demo.outputText();
    expect(secondOutput).toBe(expectedOutput, 'Second run should produce the same output and not append to the previous output');

    // Validate that the outputs are identical (verifies clearing behavior)
    expect(secondOutput).toBe(firstOutput);

    // Ensure no uncaught errors
    expect(pageErrors.length).toBe(0);
    const consoleErrors = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrors.length).toBe(0);
  });

  test('Edge case: rapid multiple clicks should not produce duplicated concatenated output', async () => {
    // Rapidly click the button multiple times
    // We click sequentially but without waiting for output in between to simulate quick user clicks
    await demo.runBtn.click();
    await demo.runBtn.click();
    await demo.runBtn.click();

    // After the rapid clicks finish, the output should reflect a single run's output (because runDemo clears before writing)
    await expect(demo.output).toHaveText(/Initial:/);
    const out = await demo.outputText();
    expect(out).toBe(expectedOutput, 'Rapid multiple clicks should still result in a single clean output from the last run');

    // Check no page errors or console errors were produced during rapid clicking
    expect(pageErrors.length).toBe(0);
    const consoleErrors = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrors.length).toBe(0);
  });

  test('Visual and DOM assertions for output area and semantics', async () => {
    // Ensure the output element exists and uses the expected "output" class
    expect(await demo.hasOutputClass()).toBe(true);

    // The output area should be empty initially and should receive monospace-like font family from CSS (basic assertion)
    const fontFamily = await demo.output.evaluate((el) => getComputedStyle(el).fontFamily);
    // The CSS sets font-family to "Consolas, monospace" — expect that 'monospace' is part of the computed family string
    expect(fontFamily.toLowerCase()).toContain('monospace');

    // Clicking produces the expected content
    await demo.clickRun();
    const content = await demo.outputInnerText();
    expect(content).toContain("Initial: number : 42");
    expect(content).toContain('Assigned object: object : {"message":"I\'m an object now"}');

    // No runtime errors observed in console or pageerror
    expect(pageErrors.length).toBe(0);
    const consoleErrors = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrors.length).toBe(0);
  });

  test('Observe console messages and page errors during navigation and interaction', async ({ page }) => {
    // This test explicitly verifies that we observe and record console/page errors correctly.
    // On this page there are no runtime console.error messages expected; assert that none exist.

    // At this point we have navigated in beforeEach. Verify no page errors so far.
    expect(pageErrors.length).toBe(0);

    // Click the button and verify still no errors
    await demo.clickRun();
    expect(pageErrors.length).toBe(0);

    // Log a summary of captured console messages (non-failing assertion for presence/absence)
    const errorConsoleMessages = consoleMessages.filter((m) => m.type === 'error');
    // Assert none of the messages are of type error
    expect(errorConsoleMessages.length).toBe(0);

    // It's acceptable if informational logs exist (none expected here), but ensure we recorded console events
    // This also validates our monitoring hooks are active.
    expect(Array.isArray(consoleMessages)).toBe(true);
  });
});