import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/f5af9921-fa7c-11f0-adc7-178f556b1ee0.html';

// Helper: wait until consoleMessages array has at least `count` entries or timeout
async function waitForConsoleCount(consoleMessages, count, timeout = 2000) {
  const start = Date.now();
  while (Date.now() - start < timeout) {
    if (consoleMessages.length >= count) return;
    await new Promise((r) => setTimeout(r, 50));
  }
  throw new Error(`Timed out waiting for ${count} console messages (got ${consoleMessages.length})`);
}

test.describe('Queue FSM - f5af9921-fa7c-11f0-adc7-178f556b1ee0', () => {
  let consoleMessages;
  let pageErrors;
  let pageConsoleListener;
  let pageErrorListener;

  test.beforeEach(async ({ page }) => {
    // Collect console messages and page errors for assertions
    consoleMessages = [];
    pageErrors = [];

    pageConsoleListener = (msg) => {
      // Capture type and text for detailed assertions
      consoleMessages.push({ type: msg.type(), text: msg.text() });
    };
    pageErrorListener = (err) => {
      // Uncaught exceptions in page context
      pageErrors.push(err);
    };

    page.on('console', pageConsoleListener);
    page.on('pageerror', pageErrorListener);

    // Navigate to the application page
    await page.goto(APP_URL);
  });

  test.afterEach(async ({ page }) => {
    // Cleanup listeners
    page.off('console', pageConsoleListener);
    page.off('pageerror', pageErrorListener);
    // Close page-specific state if needed (handled by Playwright fixture)
  });

  test('S0 Idle: Page renders and initial UI elements are present', async ({ page }) => {
    // This test validates the Idle state: the page is rendered and the "Demonstrate Queue Operations" button exists.
    const trigger = page.locator('#trigger-demo');
    await expect(trigger).toBeVisible();
    await expect(trigger).toHaveText('Demonstrate Queue Operations');

    // Verify static content is present (basic smoke checks)
    await expect(page.locator('h1')).toHaveText('Queue');
    await expect(page.locator('h2', { hasText: 'Queue Algorithms' })).toBeVisible();

    // There should be no uncaught page errors prior to interaction
    expect(pageErrors.length).toBe(0);
    // There may be no console errors initially; assert that explicitly
    const initialConsoleErrors = consoleMessages.filter((m) => m.type === 'error');
    expect(initialConsoleErrors.length).toBe(0);
  });

  test('Transition: DemonstrateQueueOperations - clicking the button logs queue operations and updates internal state', async ({ page }) => {
    // This test validates the transition from S0_Idle to S1_QueueDemonstrated:
    // - Clicking the button should produce console logs for enqueue/dequeue/peek/isEmpty/size.
    // - Internal queue state (queue and elements arrays) should reflect operations.
    // - Ensure no uncaught exceptions occur during the demonstration.

    // Prepare to capture console logs generated by the click handler
    const trigger = page.locator('#trigger-demo');
    await expect(trigger).toBeVisible();

    // Click the demonstration button
    await trigger.click();

    // The implementation emits 7 console.log calls in one click handler invocation.
    // Wait until we have at least those console messages collected.
    await waitForConsoleCount(consoleMessages, 7, 2000);

    // Extract just the text strings from captured console messages for easier assertions
    const texts = consoleMessages.map((m) => m.text);

    // We expect three "Enqueue" logs (note: the implementation logs the return value of enqueue which is undefined)
    const enqueueMsgs = texts.filter((t) => t.includes('Enqueue:'));
    expect(enqueueMsgs.length).toBeGreaterThanOrEqual(3);

    // Check for Dequeue, Peek, IsEmpty, and Size logs with their actual values produced by the code
    expect(texts.some((t) => t.includes('Dequeue:') && t.includes('Element 1'))).toBeTruthy();
    // The implementation keeps a separate `elements` array and does not remove from it on dequeue,
    // so peek() will still return "Element 1" according to the actual code.
    expect(texts.some((t) => t.includes('Peek:') && t.includes('Element 1'))).toBeTruthy();
    expect(texts.some((t) => t.includes('IsEmpty:') && t.includes('false'))).toBeTruthy();
    expect(texts.some((t) => t.includes('Size:') && t.includes('2'))).toBeTruthy();

    // The FSM expected "Peek: Element 2" but the actual implementation returns "Element 1".
    // Assert that the FSM-expected string is NOT present to surface the behavioral mismatch.
    const fsmExpectedPeek = texts.some((t) => t.includes('Peek:') && t.includes('Element 2'));
    expect(fsmExpectedPeek).toBeFalsy();

    // Verify internal runtime state via page.evaluate:
    // - 'queue' should reflect that one element was dequeued: size 2 and content starts with "Element 2"
    // - 'elements' should contain all enqueued elements (3)
    const runtimeQueue = await page.evaluate(() => {
      // Accessing the page-scoped variables as defined in the script
      return { queueSnapshot: queue.slice(), elementsSnapshot: elements.slice() };
    });

    expect(Array.isArray(runtimeQueue.queueSnapshot)).toBeTruthy();
    expect(Array.isArray(runtimeQueue.elementsSnapshot)).toBeTruthy();

    // Queue should have two elements after one dequeue in the demo click
    expect(runtimeQueue.queueSnapshot.length).toBe(2);
    // The front of the queue (after dequeue) should be "Element 2"
    expect(runtimeQueue.queueSnapshot[0]).toBe('Element 2');
    expect(runtimeQueue.queueSnapshot[1]).toBe('Element 3');

    // Elements array retains all enqueued elements (implementation bug/feature as per code)
    expect(runtimeQueue.elementsSnapshot.length).toBe(3);
    expect(runtimeQueue.elementsSnapshot[0]).toBe('Element 1');

    // Ensure no uncaught exceptions were thrown during the demo
    expect(pageErrors.length).toBe(0);

    // There should be no console messages of type 'error' related to runtime failures
    const consoleErrorMsgs = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrorMsgs.length).toBe(0);
  });

  test('Edge case: dequeue on an empty queue returns "Queue is empty"', async ({ page }) => {
    // This test validates behavior when dequeue is invoked while the queue is empty.
    // Reload the page to ensure fresh state (empty queue)
    await page.reload();

    // Directly invoke dequeue from the page context (simulate API call when empty)
    const result = await page.evaluate(() => {
      // At page load, queue is an empty array; calling dequeue should return the empty message
      return dequeue();
    });

    expect(result).toBe('Queue is empty');

    // Confirm that this action did not produce uncaught page errors
    expect(pageErrors.length).toBe(0);
    const consoleErrorMsgs = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrorMsgs.length).toBe(0);
  });

  test('Edge case: enqueue returns undefined (no explicit return) and pushes into arrays', async ({ page }) => {
    // This test validates that enqueue does not return the enqueued element (undefined) and that it mutates internal arrays.
    // Ensure fresh state
    await page.reload();

    const enqueueReturn = await page.evaluate(() => {
      return enqueue('TestElementX');
    });

    // The function does not return anything explicitly; the returned value should be undefined
    expect(enqueueReturn).toBeUndefined();

    // Check that both arrays were mutated
    const state = await page.evaluate(() => {
      return { q: queue.slice(), el: elements.slice() };
    });

    expect(state.q.length).toBe(1);
    expect(state.el.length).toBe(1);
    expect(state.q[0]).toBe('TestElementX');
    expect(state.el[0]).toBe('TestElementX');

    // Again, no runtime errors expected
    expect(pageErrors.length).toBe(0);
    const consoleErrorMsgs = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrorMsgs.length).toBe(0);
  });

  test('Idempotency / repeated transition: clicking demo multiple times updates queue cumulatively', async ({ page }) => {
    // This test clicks the demo button twice and verifies the cumulative effect on internal state and logs.
    // Reset state by reloading
    await page.reload();

    const trigger = page.locator('#trigger-demo');
    await expect(trigger).toBeVisible();

    // First click
    await trigger.click();
    await waitForConsoleCount(consoleMessages, 7, 2000);

    // Capture state after first click
    const afterFirst = await page.evaluate(() => ({ q: queue.slice(), el: elements.slice() }));
    expect(afterFirst.q.length).toBe(2);
    expect(afterFirst.el.length).toBe(3);

    // Clear collected console messages array to isolate second click logs
    consoleMessages.length = 0;

    // Second click
    await trigger.click();
    // The second click produces another 7 logs
    await waitForConsoleCount(consoleMessages, 7, 2000);

    // Inspect state after second click
    const afterSecond = await page.evaluate(() => ({ q: queue.slice(), el: elements.slice() }));

    // After second demo click:
    // - Each click enqueues 3, dequeues 1 => net +2 in queue length per click.
    // After two clicks, expected queue length = 4 (2 after first; +2 after second)
    expect(afterSecond.q.length).toBe(4);
    // Elements array accumulates every enqueue without removal, so after 2 clicks it should have 6 entries
    expect(afterSecond.el.length).toBe(6);

    // Check that the console logs from the second click include a Dequeue log (the dequeued element should be the element at front at that time)
    const afterSecondTexts = consoleMessages.map((m) => m.text);
    expect(afterSecondTexts.some((t) => t.includes('Dequeue:'))).toBeTruthy();

    // Ensure no page errors throughout repeated transitions
    expect(pageErrors.length).toBe(0);
    const consoleErrorMsgs = consoleMessages.filter((m) => m.type === 'error');
    expect(consoleErrorMsgs.length).toBe(0);
  });
});