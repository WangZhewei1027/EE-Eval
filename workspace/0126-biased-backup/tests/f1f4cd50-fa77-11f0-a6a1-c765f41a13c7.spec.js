import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/f1f4cd50-fa77-11f0-a6a1-c765f41a13c7.html';

// Page Object for the Hash Map visualization app
class HashMapPage {
  constructor(page) {
    this.page = page;
  }

  async goto() {
    await this.page.goto(APP_URL, { waitUntil: 'load' });
    // wait a tick for requestAnimationFrame snapshotInitial to run
    await this.page.waitForTimeout(50);
  }

  async getAnimateButton() {
    return this.page.locator('#animate');
  }

  async getResetButton() {
    return this.page.locator('#reset');
  }

  async getPoolChipCount() {
    return await this.page.locator('#pool .chip').count();
  }

  async getBucketsChipCount() {
    // sums chips inside all bucket .values containers
    return await this.page.evaluate(() => {
      const vals = Array.from(document.querySelectorAll('.bucket .values'));
      return vals.reduce((acc, el) => acc + el.querySelectorAll('.chip').length, 0);
    });
  }

  async clickAnimate() {
    await this.page.click('#animate');
  }

  async clickReset() {
    await this.page.click('#reset');
  }

  async clickBucket(index = 0) {
    // click the bucket at given index
    await this.page.click(`#bucketList .bucket[data-index="${index}"]`);
  }

  async pressKey(key) {
    await this.page.keyboard.press(key);
  }

  // Wait until the animate button becomes enabled (animation finished)
  async waitForAnimationToComplete(timeout = 8000) {
    await this.page.waitForFunction(() => {
      const btn = document.getElementById('animate');
      return btn && !btn.disabled;
    }, null, { timeout });
  }

  // Wait until animate button is disabled (animation started)
  async waitForAnimationStart(timeout = 2000) {
    await this.page.waitForFunction(() => {
      const btn = document.getElementById('animate');
      return btn && btn.disabled;
    }, null, { timeout });
  }
}

test.describe('Hash Map visualization - FSM states and transitions', () => {
  // capture console errors and page errors for each test
  test.beforeEach(async ({ page }) => {
    // ensure we start with a fresh listener set per test
    page._consoleErrors = [];
    page._pageErrors = [];

    page.on('console', msg => {
      if (msg.type() === 'error') {
        page._consoleErrors.push({ text: msg.text(), location: msg.location() });
      }
    });

    page.on('pageerror', err => {
      // pageerror corresponds to uncaught exceptions
      page._pageErrors.push(err);
    });
  });

  // Test Idle state: S0_Idle
  test('S0_Idle: initial render shows controls and pool chips', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Verify animate and reset buttons are present and enabled (Idle)
    const animateBtn = await app.getAnimateButton();
    const resetBtn = await app.getResetButton();
    await expect(animateBtn).toBeVisible();
    await expect(resetBtn).toBeVisible();
    await expect(animateBtn).toBeEnabled();
    await expect(resetBtn).toBeEnabled();

    // Verify pool contains the expected chips (keys are generated by JS)
    const poolCount = await app.getPoolChipCount();
    // According to implementation keys array has 10 entries
    expect(poolCount).toBe(10);

    // Verify there are N buckets rendered (N = 7)
    const bucketCount = await page.locator('#bucketList .bucket').count();
    expect(bucketCount).toBe(7);

    // Ensure no console errors or uncaught page errors happened during load
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Test AnimateMapping event and transition S0_Idle -> S1_Animating
  test('AnimateMapping (click #animate) moves chips to buckets and toggles animating state', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Ensure starting in Idle: pool has all chips
    expect(await app.getPoolChipCount()).toBe(10);
    expect(await app.getBucketsChipCount()).toBe(0);

    // Click animate to start mapping animation
    // We will ensure the button becomes disabled (animating) then becomes enabled again (animation complete)
    await app.clickAnimate();

    // Verify animation started: animate button disabled promptly
    await app.waitForAnimationStart(2000);
    const animateBtn = await app.getAnimateButton();
    await expect(animateBtn).toBeDisabled();

    // While animating, clicking reset should be ignored per implementation (edge case)
    // We simulate a rapid reset click and then confirm animation completes normally
    await app.clickReset();
    // Allow animation to finish (function waits up to ~6.5s in implementation)
    await app.waitForAnimationToComplete(9000);

    // After animation completes, animate button should be enabled
    await expect(animateBtn).toBeEnabled();

    // The pool should now be empty (chips moved into buckets)
    expect(await app.getPoolChipCount()).toBe(0);

    // Buckets should contain all chips
    const totalInBuckets = await app.getBucketsChipCount();
    expect(totalInBuckets).toBe(10);

    // No console errors or uncaught page errors should have been emitted during animation
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Test ResetLayout event from S1_Animating (reset click during animating is ignored)
  test('ResetLayout during animating is ignored; chips still map to buckets after animation', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Start animation
    await app.clickAnimate();
    await app.waitForAnimationStart(2000);

    // Immediately click reset while animating; implementation contains guard if(animating) return;
    await app.clickReset();

    // Wait for animation to finish
    await app.waitForAnimationToComplete(9000);

    // Confirm that chips ended up in buckets (reset did not prematurely move chips back)
    expect(await app.getPoolChipCount()).toBe(0);
    expect(await app.getBucketsChipCount()).toBe(10);

    // Now click reset after animation completes -> should return chips to pool
    await app.clickReset();
    // Allow some time for reset animation to complete (implementation sets timeouts ~720ms)
    await page.waitForTimeout(900);
    expect(await app.getPoolChipCount()).toBe(10);
    expect(await app.getBucketsChipCount()).toBe(0);

    // No unexpected errors should have occurred
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Test ResetLayout from Idle: S0_Idle -> S2_Resetting
  test('ResetLayout when idle returns chips to pool (idempotent reset)', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Ensure idle: pool has all chips
    expect(await app.getPoolChipCount()).toBe(10);

    // Click reset in idle state (should not cause issues)
    await app.clickReset();
    // Wait briefly for any transitions to settle
    await page.waitForTimeout(500);

    // Pool should remain with all chips
    expect(await app.getPoolChipCount()).toBe(10);
    expect(await app.getBucketsChipCount()).toBe(0);

    // No console errors or uncaught page errors
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Test HighlightBucket: clicking a bucket triggers the brief highlight animation
  test('HighlightBucket: clicking a bucket triggers highlight animation (no errors)', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Click bucket 2 and ensure no chips move and no errors occur
    const prePool = await app.getPoolChipCount();
    await app.clickBucket(2);

    // The animate() call on the bucket is ephemeral; give it time to run
    await page.waitForTimeout(450);

    // Ensure the pool and buckets remain unchanged
    expect(await app.getPoolChipCount()).toBe(prePool);
    expect(await app.getBucketsChipCount()).toBe(0);

    // No console errors or uncaught page errors
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Test KeyDownEnter triggers AnimateMapping (S1_Animating) and KeyDownEscape triggers ResetLayout
  test('Keydown Enter triggers animate; Escape triggers reset (keyboard shortcuts)', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Ensure idle
    expect(await app.getPoolChipCount()).toBe(10);

    // Press Enter to start animation (keyboard shortcut wired to animateBtn.click())
    await app.pressKey('Enter');

    // Confirm animation started (button disabled)
    await app.waitForAnimationStart(2000);
    const animateBtn = await app.getAnimateButton();
    await expect(animateBtn).toBeDisabled();

    // Wait for animation to finish
    await app.waitForAnimationToComplete(9000);

    // Confirm chips in buckets
    expect(await app.getPoolChipCount()).toBe(0);
    expect(await app.getBucketsChipCount()).toBe(10);

    // Press Escape to reset (keyboard shortcut wired to resetBtn.click())
    await app.pressKey('Escape');

    // Wait for reset animation to complete
    await page.waitForTimeout(900);
    expect(await app.getPoolChipCount()).toBe(10);
    expect(await app.getBucketsChipCount()).toBe(0);

    // No console errors or uncaught page errors
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Edge case: repeated animate clicks should not break (second click while animating is ignored)
  test('Clicking animate repeatedly does not cause exceptions and only a single animation runs at a time', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Rapidly click animate multiple times
    await app.clickAnimate();
    await app.clickAnimate();
    await app.clickAnimate();

    // The button should be disabled while animation runs
    await app.waitForAnimationStart(2000);
    const animateBtn = await app.getAnimateButton();
    await expect(animateBtn).toBeDisabled();

    // Wait for completion
    await app.waitForAnimationToComplete(9000);

    // After completion, expect all chips moved exactly once into buckets
    expect(await app.getPoolChipCount()).toBe(0);
    expect(await app.getBucketsChipCount()).toBe(10);

    // Reset back to pool for cleanliness
    await app.clickReset();
    await page.waitForTimeout(900);
    expect(await app.getPoolChipCount()).toBe(10);

    // Assert there were no console error messages or uncaught exceptions
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });

  // Verify that the implementation's "renderPage()" entry action (S0_Idle) has run by checking visible content
  test('On entry to Idle (S0_Idle) the page content is rendered (title, description, controls)', async ({ page }) => {
    const app = new HashMapPage(page);
    await app.goto();

    // Check header content and description presence as evidence of renderPage()
    await expect(page.locator('h1', { hasText: 'Hash Map' })).toBeVisible();
    await expect(page.locator('.desc')).toBeVisible();
    await expect(page.locator('#animate')).toBeVisible();
    await expect(page.locator('#reset')).toBeVisible();

    // No console errors or uncaught exceptions
    expect(page._consoleErrors.length).toBe(0);
    expect(page._pageErrors.length).toBe(0);
  });
});