import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/ed90afe0-fa77-11f0-8492-31e949ed3c7c.html';

// Expected alert text from the implementation
const EXPECTED_ALERT_TEXT =
  'Digital signatures are crucial for security in online communications. They protect the integrity of your data!';

test.describe('ed90afe0-fa77-11f0-8492-31e949ed3c7c - Digital Signatures FSM tests', () => {
  // Arrays to collect runtime console errors and page errors for assertions
  let consoleErrors;
  let pageErrors;

  // Setup before each test: navigate to the page and attach listeners to capture console and page errors
  test.beforeEach(async ({ page }) => {
    consoleErrors = [];
    pageErrors = [];

    // Capture console error messages
    page.on('console', (msg) => {
      if (msg.type() === 'error') {
        consoleErrors.push(msg.text());
      }
    });

    // Capture uncaught page errors (e.g. ReferenceError, TypeError thrown in page)
    page.on('pageerror', (err) => {
      // push the actual Error object so tests can inspect type/name/message if needed
      pageErrors.push(err);
    });

    // Navigate to the application page
    await page.goto(APP_URL);
    // Ensure initial load completed
    await expect(page).toHaveURL(APP_URL);
  });

  test.afterEach(async ({ page }) => {
    // No teardown modifications to the page; just ensure there are no lingering dialogs
    // If a dialog is open, try to close it to avoid interfering with other tests
    // Note: This tries gracefully, but does not modify application logic.
    try {
      // Accept any modal dialog if still open (defensive).
      // Playwright does not provide a direct API to detect an open dialog other than listening events,
      // so this is intentionally minimal and non-invasive.
    } catch (e) {
      // ignore
    }
  });

  test('Initial (Idle) state renders correctly: DOM elements and visual components exist', async ({ page }) => {
    // Validate presence of main content elements that represent the Idle state
    // Comment: This test verifies the initial rendering (S0_Idle) evidence: button & image exist and text is correct.

    const container = page.locator('.container');
    await expect(container).toBeVisible();

    // Heading exists and contains expected text
    const heading = page.locator('h1');
    await expect(heading).toHaveText(/Understanding Digital Signatures/i);

    // Paragraph contains the phrase that explains digital signatures
    const paragraph = page.locator('p');
    await expect(paragraph).toContainText('electronic fingerprint');

    // Validate "Learn More" button exists with expected onclick attribute selector evidence
    const learnMoreButton = page.locator("button[onclick='showMoreInfo()']");
    await expect(learnMoreButton).toHaveCount(1);
    await expect(learnMoreButton).toBeVisible();
    await expect(learnMoreButton).toHaveText('Learn More');

    // Validate image evidence from FSM: src and alt attributes
    const graphic = page.locator('img.graphic[alt="Digital Signature Concept"]');
    await expect(graphic).toHaveCount(1);
    const src = await graphic.getAttribute('src');
    expect(src).toContain('Digital+Signature+Graphic'); // spot-check the placeholder URL piece

    // Verify that the showMoreInfo function exists on the page (so the button's onclick should work)
    const showMoreInfoType = await page.evaluate(() => typeof window.showMoreInfo);
    expect(showMoreInfoType).toBe('function');

    // Verify that the declared FSM entry action renderPage() is NOT defined in the actual implementation
    // (The FSM mentions renderPage() as an entry action for S0_Idle; we check whether it exists.)
    const renderPageType = await page.evaluate(() => typeof window.renderPage);
    expect(renderPageType).toBe('undefined');

    // Assert no uncaught runtime page errors occurred during load
    expect(pageErrors.length, 'No uncaught page errors on initial load').toBe(0);
    expect(consoleErrors.length, 'No console.error messages on initial load').toBe(0);
  });

  test('Transition: clicking "Learn More" triggers alert -> AlertShown state', async ({ page }) => {
    // Comment: This test validates the FSM transition S0_Idle -> S1_AlertShown by clicking the Learn More button
    // It captures the dialog, verifies its message, and accepts it (simulating the user acknowledging the alert).

    // Prepare to capture the dialog generated by showMoreInfo()
    const [dialog] = await Promise.all([
      page.waitForEvent('dialog'), // wait for the dialog event
      page.click("button[onclick='showMoreInfo()']"), // trigger the event
    ]);

    // Validate the alert message content matches the FSM evidence
    expect(dialog.type()).toBe('alert');
    expect(dialog.message()).toBe(EXPECTED_ALERT_TEXT);

    // Accept (close) the alert to restore page interactivity
    await dialog.accept();

    // After the alert is handled, ensure no unexpected runtime errors were produced
    expect(pageErrors.length, 'No uncaught page errors after clicking Learn More').toBe(0);
    expect(consoleErrors.length, 'No console.error messages after clicking Learn More').toBe(0);
  });

  test('Edge case: multiple rapid clicks produce sequential alerts that can be handled', async ({ page }) => {
    // Comment: This test simulates rapid repeated clicks on the Learn More button.
    // Since alerts are modal, we handle each dialog sequentially and assert each alert's content.

    const buttonSelector = "button[onclick='showMoreInfo()']";
    const messages = [];

    // Click and handle dialog twice in sequence
    for (let i = 0; i < 2; i++) {
      // Trigger the click, then wait for the dialog event and accept it
      const clickPromise = page.click(buttonSelector);
      const dialog = await page.waitForEvent('dialog');
      // Ensure the dialog message is correct
      messages.push(dialog.message());
      await dialog.accept();
      // wait for the click action to resolve to avoid racing
      await clickPromise;
    }

    // Both dialogs should have the expected message
    expect(messages.length).toBe(2);
    for (const msg of messages) {
      expect(msg).toBe(EXPECTED_ALERT_TEXT);
    }

    // Assert no page errors or console.error messages were produced by rapid clicks
    expect(pageErrors.length, 'No uncaught page errors after rapid clicks').toBe(0);
    expect(consoleErrors.length, 'No console.error messages after rapid clicks').toBe(0);
  });

  test('FSM entry action validation: calling missing renderPage() produces a ReferenceError in page context', async ({ page }) => {
    // Comment: The FSM lists renderPage() as an entry action for S0_Idle.
    // This implementation does not define renderPage. We intentionally call it in the page context
    // and assert that a ReferenceError is thrown naturally (we do NOT patch or define renderPage).
    const result = await page.evaluate(() => {
      try {
        // This call should throw a ReferenceError because renderPage is not defined
        // The error will be caught and returned for assertion in the test context
        renderPage();
        return { threw: false };
      } catch (e) {
        return { threw: true, name: e && e.name, message: e && e.message };
      }
    });

    // Validate that a ReferenceError was observed in the page context
    expect(result.threw).toBe(true);
    // The exact error name in browser contexts for calling an undefined function is typically ReferenceError
    expect(result.name).toBe('ReferenceError');
    // Ensure the message mentions renderPage to show it was the missing identifier that caused it
    expect(result.message).toContain('renderPage');

    // Confirm no additional page errors were emitted via pageerror event (this caught the thrown error within evaluate)
    // Note: Because the evaluate wrapped the throw in a try-catch, it may not generate an uncaught pageerror.
    expect(pageErrors.length).toBe(0);
  });

  test('Error scenario: clicking a non-existent selector leads to a Playwright timeout/selector error', async ({ page }) => {
    // Comment: This test intentionally attempts to click a selector that does not exist on the page.
    // We observe that Playwright throws an error for missing selectors - we capture that error and assert it occurs.
    const missingSelector = 'button#does-not-exist';

    // First assert that locator count is zero
    const count = await page.locator(missingSelector).count();
    expect(count).toBe(0);

    // Attempt to click the missing selector and expect an error to be thrown by Playwright
    let threw = false;
    let thrownMessage = '';
    try {
      // Use a short timeout to fail fast
      await page.click(missingSelector, { timeout: 1000 });
    } catch (err) {
      threw = true;
      thrownMessage = err && err.message ? err.message : String(err);
    }

    expect(threw, 'Clicking a non-existent selector should throw an error').toBe(true);
    // The thrown message should mention waiting for selector or not found (Playwright's typical message)
    expect(thrownMessage.toLowerCase()).toMatch(/waiting for selector|no element|has not been found|failed to find/i);
  });
});