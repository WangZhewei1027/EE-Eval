// Page Object to encapsulate interactions with BFS visualization page
class BFSPage {
  /**
   * @param {import('@playwright/test').Page} page
   */
  constructor(page) {
    this.page = page;
    this.startBtn = page.locator('#start-btn');
    this.resetBtn = page.locator('#reset-btn');
    this.svg = page.locator('svg');
    this.nodeLocator = (id) => page.locator(`g.node[data-id="${id}"]`);
    this.visitedNodes = page.locator('g.node.visited');
    this.edgeVisited = page.locator('line.edge.visited');
    this.edgesAll = page.locator('line.edge');
    this.nodesAll = page.locator('g.node');
  }

  async goto(url) {
    await this.page.goto(url);
  }

  async clickStart() {
    await this.startBtn.click();
  }

  async clickReset() {
    await this.resetBtn.click();
  }

  // Wait until at least `count` nodes have the visited class
  async waitForVisitedNodesCount(count, timeout = 20000) {
    await this.page.waitForFunction(
      (expected) => {
        const nodes = document.querySelectorAll('g.node.visited');
        return nodes.length >= expected;
      },
      count,
      { timeout }
    );
  }

  // Wait until exactly `count` nodes have the visited class
  async waitForVisitedNodesExact(count, timeout = 20000) {
    await this.page.waitForFunction(
      (expected) => {
        const nodes = document.querySelectorAll('g.node.visited');
        return nodes.length === expected;
      },
      count,
      { timeout }
    );
  }
}

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/3c978d71-fa78-11f0-857d-d58e82d5de73.html';

test.describe('BFS Visualization - FSM states and transitions', () => {
  // Will collect console error messages and page errors for assertions
  let consoleErrors;
  let pageErrors;

  test.beforeEach(async ({ page }) => {
    consoleErrors = [];
    pageErrors = [];

    // Capture console messages (filter for errors)
    page.on('console', (msg) => {
      if (msg.type() === 'error') {
        consoleErrors.push({
          text: msg.text(),
          location: msg.location()
        });
      }
    });

    // Capture uncaught exceptions on the page
    page.on('pageerror', (err) => {
      pageErrors.push(err);
    });
  });

  test.describe('Initial Idle state (S0_Idle)', () => {
    test('renders graph and control buttons in initial idle state', async ({ page }) => {
      // Arrange: load page and create page object
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Assert: start button should be enabled and reset button disabled (evidence in FSM)
      await expect(bfs.startBtn).toBeEnabled();
      await expect(bfs.resetBtn).toBeDisabled();

      // Assert: SVG exists and nodes/edges were generated by createGraph()
      await expect(bfs.svg).toBeVisible();

      // Expect 7 nodes and 8 edges as per implementation
      await expect(bfs.nodesAll).toHaveCount(7);
      await expect(bfs.edgesAll).toHaveCount(8);

      // Check special nodes present (start and end classes on specific nodes)
      const startNode = await page.locator('g.node.start').first();
      const endNode = await page.locator('g.node.end').first();
      await expect(startNode).toBeVisible();
      await expect(endNode).toBeVisible();

      // Confirm DOM attributes and accessibility labels for controls exist
      await expect(bfs.startBtn).toHaveAttribute('aria-controls', 'graph-container');
      await expect(bfs.startBtn).toHaveAttribute('aria-label', 'Start Breadth-First Search Visualization');
      await expect(bfs.resetBtn).toHaveAttribute('aria-controls', 'graph-container');
      await expect(bfs.resetBtn).toHaveAttribute('aria-label', 'Reset Visualization');

      // Verify there are no uncaught page errors or console error messages on initial render
      expect(pageErrors.length, `Expected no page errors on initial load but got: ${pageErrors.map(e => String(e)).join('; ')}`).toBe(0);
      expect(consoleErrors.length, `Expected no console error messages on initial load but got: ${consoleErrors.map(e => e.text).join('; ')}`).toBe(0);
    });
  });

  test.describe('Start BFS event and Animating state (S1_Animating)', () => {
    test('clicking Start BFS enters Animating: start disabled, reset enabled, nodes/edges start to be visited', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Sanity checks before action
      await expect(bfs.startBtn).toBeEnabled();
      await expect(bfs.resetBtn).toBeDisabled();

      // Act: click start
      await bfs.clickStart();

      // Immediately after starting, Start should be disabled and Reset enabled (entry/exit evidence)
      await expect(bfs.startBtn).toBeDisabled();
      await expect(bfs.resetBtn).toBeEnabled();

      // The first bfsStep runs immediately; the start node (A) should become visited right away
      const startNodeVisited = page.locator('g.node[data-id="A"].visited');
      await expect(startNodeVisited).toBeVisible({ timeout: 2000 });

      // Edges out of A (A->B and A->C) should be highlighted (visited) by the first step
      // Wait for at least one visited edge
      await page.waitForFunction(() => document.querySelectorAll('line.edge.visited').length >= 1, null, { timeout: 3000 });

      // Confirm at least one edge has the visited class
      const visitedEdgeCount = await page.locator('line.edge.visited').count();
      expect(visitedEdgeCount).toBeGreaterThanOrEqual(1);

      // Verify no errors logged while animation started
      expect(pageErrors.length, `Page errors occurred after starting BFS: ${pageErrors.map(e => String(e)).join('; ')}`).toBe(0);
      expect(consoleErrors.length, `Console errors occurred after starting BFS: ${consoleErrors.map(e => e.text).join('; ')}`).toBe(0);
    });

    test('clicking Start again while animating does not throw and keeps single animation running', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      await bfs.clickStart();

      // Click start again quickly - startBFS guards against double-start by checking animationInterval
      await bfs.startBtn.click();

      // State should still reflect animating: start disabled, reset enabled
      await expect(bfs.startBtn).toBeDisabled();
      await expect(bfs.resetBtn).toBeEnabled();

      // Ensure that nodes are being visited (at least start node)
      await expect(page.locator('g.node[data-id="A"].visited')).toBeVisible({ timeout: 2000 });

      // No console or page errors due to double-click
      expect(pageErrors.length).toBe(0);
      expect(consoleErrors.length).toBe(0);
    });
  });

  test.describe('Animating -> Completed (S1_Animating to S2_Completed)', () => {
    test('animation finishes and page reaches Completed state: all nodes visited and controls toggled', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Start the BFS animation
      await bfs.clickStart();

      // Wait until all nodes are visited (exactly 7)
      // BFS should visit each of the 7 nodes; allow ample timeout for the full animation
      await bfs.waitForVisitedNodesExact(7, 20000);

      // After completion, start button should remain disabled and reset button enabled (Completed evidence)
      await expect(bfs.startBtn).toBeDisabled();
      await expect(bfs.resetBtn).toBeEnabled();

      // All edges that should be visited along BFS paths should have class 'visited'
      const visitedEdgesCount = await page.locator('line.edge.visited').count();
      // All eight edges may not all be 'visited' if BFS doesn't traverse redundant edges; require at least > 0
      expect(visitedEdgesCount).toBeGreaterThanOrEqual(1);

      // There should be no uncaught exceptions or console errors during the full run
      expect(pageErrors.length, `Page errors occurred during full animation: ${pageErrors.map(e => String(e)).join('; ')}`).toBe(0);
      expect(consoleErrors.length, `Console errors occurred during full animation: ${consoleErrors.map(e => e.text).join('; ')}`).toBe(0);
    });
  });

  test.describe('Reset Visualization (S2_Completed -> S0_Idle) and edge cases', () => {
    test('clicking Reset after completion resets the visualization to Idle', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Start and wait for completion
      await bfs.clickStart();
      await bfs.waitForVisitedNodesExact(7, 20000);

      // Now click reset to go back to Idle
      await bfs.clickReset();

      // Start should be enabled again and reset disabled - evidence of Idle state
      await expect(bfs.startBtn).toBeEnabled();
      await expect(bfs.resetBtn).toBeDisabled();

      // No nodes or edges should have 'visited' class
      const visitedNodesCountAfterReset = await page.locator('g.node.visited').count();
      const visitedEdgesCountAfterReset = await page.locator('line.edge.visited').count();
      expect(visitedNodesCountAfterReset).toBe(0);
      expect(visitedEdgesCountAfterReset).toBe(0);

      // No errors during reset
      expect(pageErrors.length).toBe(0);
      expect(consoleErrors.length).toBe(0);
    });

    test('clicking Reset while animating stops animation and resets to Idle (edge case)', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Start animation
      await bfs.clickStart();

      // Ensure animation has started (start disabled)
      await expect(bfs.startBtn).toBeDisabled();

      // Wait a short while to ensure interval is active and some progress may have happened
      await page.waitForTimeout(600);

      // Click reset during animation - resetVisualization should clear the interval and reset state
      await bfs.clickReset();

      // Start should be enabled and reset disabled after reset
      await expect(bfs.startBtn).toBeEnabled();
      await expect(bfs.resetBtn).toBeDisabled();

      // No nodes/edges visited after reset
      const visitedNodesCount = await page.locator('g.node.visited').count();
      const visitedEdgesCount = await page.locator('line.edge.visited').count();
      expect(visitedNodesCount).toBe(0);
      expect(visitedEdgesCount).toBe(0);

      // Ensure no uncaught exceptions/errors were triggered by stopping the animation early
      expect(pageErrors.length, `Page errors occurred when resetting during animation: ${pageErrors.map(e => String(e)).join('; ')}`).toBe(0);
      expect(consoleErrors.length, `Console errors occurred when resetting during animation: ${consoleErrors.map(e => e.text).join('; ')}`).toBe(0);
    });

    test('pressing Reset when already in Idle is a no-op and should not error (edge case)', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Ensure we're in Idle
      await expect(bfs.startBtn).toBeEnabled();
      await expect(bfs.resetBtn).toBeDisabled();

      // Click reset when disabled (should be a no-op)
      // Using JS to click disabled button may throw in browser, so guard: only click if enabled
      const resetDisabled = await bfs.resetBtn.isDisabled();
      if (!resetDisabled) {
        await bfs.clickReset();
      }

      // State remains Idle
      await expect(bfs.startBtn).toBeEnabled();
      await expect(bfs.resetBtn).toBeDisabled();

      // No console or page errors should have been produced
      expect(pageErrors.length).toBe(0);
      expect(consoleErrors.length).toBe(0);
    });
  });

  test.describe('Observability: console and page errors monitoring', () => {
    test('no unexpected console errors or uncaught exceptions occur during typical flows', async ({ page }) => {
      const bfs = new BFSPage(page);
      await bfs.goto(APP_URL);

      // Run through a typical flow: start -> wait some nodes -> reset
      await bfs.clickStart();

      // Wait for at least 2 nodes visited to ensure animation progressed
      await bfs.waitForVisitedNodesCount(2, 8000);

      await bfs.clickReset();

      // Assert that there were no page errors or console errors during this flow
      expect(pageErrors.length, `Page errors were emitted: ${pageErrors.map(e => String(e)).join('; ')}`).toBe(0);
      expect(consoleErrors.length, `Console errors were emitted: ${consoleErrors.map(e => e.text).join('; ')}`).toBe(0);
    });
  });
});