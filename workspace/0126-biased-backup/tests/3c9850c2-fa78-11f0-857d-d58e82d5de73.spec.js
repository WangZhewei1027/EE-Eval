import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/3c9850c2-fa78-11f0-857d-d58e82d5de73.html';

// Page object encapsulating interactions and queries for the visualization page
class VizPage {
  /**
   * @param {import('@playwright/test').Page} page
   */
  constructor(page) {
    this.page = page;
    this.stepBtn = page.locator('#stepBtn');
    this.resetBtn = page.locator('#resetBtn');
    this.capacityValue = page.locator('#capacityValue');
    this.viz = page.locator('#viz');
    this.bars = page.locator('#viz .bar');
    this.selectedBars = page.locator('#viz .bar.selected');
  }

  async goto() {
    await this.page.goto(APP_URL);
    // Wait for bars to be generated by the page's initViz
    await this.page.waitForSelector('#viz .bar', { timeout: 5000 });
  }

  async getCapacityText() {
    return (await this.capacityValue.textContent()).trim();
  }

  async clickStep() {
    await this.stepBtn.click();
  }

  async clickReset() {
    await this.resetBtn.click();
  }

  async getBarsCount() {
    return await this.bars.count();
  }

  async getSelectedCount() {
    return await this.selectedBars.count();
  }

  // Return labels of selected bars in DOM order
  async getSelectedLabels() {
    const count = await this.selectedBars.count();
    const labels = [];
    for (let i = 0; i < count; i++) {
      const label = await this.selectedBars.nth(i).locator('.label').textContent();
      labels.push(label.trim());
    }
    return labels;
  }

  // Return labels of all bars in DOM order
  async getAllLabels() {
    const count = await this.bars.count();
    const labels = [];
    for (let i = 0; i < count; i++) {
      const label = await this.bars.nth(i).locator('.label').textContent();
      labels.push(label.trim());
    }
    return labels;
  }

  async isStepDisabled() {
    return await this.stepBtn.isDisabled();
  }
}

test.describe('Greedy Algorithms — Elegant Visualization (FSM tests)', () => {
  // Collect console errors and page errors to assert no runtime errors occur
  let consoleErrors;
  let pageErrors;

  test.beforeEach(async ({ page }) => {
    consoleErrors = [];
    pageErrors = [];

    // Capture console.error messages
    page.on('console', msg => {
      try {
        if (msg.type() === 'error') {
          consoleErrors.push(msg.text());
        }
      } catch (e) {
        // ignore instrumentation errors
      }
    });

    // Capture uncaught exceptions (pageerror)
    page.on('pageerror', err => {
      try {
        pageErrors.push(String(err && err.message ? err.message : err));
      } catch (e) {
        // ignore
      }
    });
  });

  test('Initial state: Initialized (S0_Initialized) - initViz executed and DOM is prepared', async ({ page }) => {
    const viz = new VizPage(page);
    // Navigate and wait for initialization
    await viz.goto();

    // Validate capacity initial value is MAX_CAPACITY (expected "50")
    const cap = await viz.getCapacityText();
    // Capacity display should be "50" initially per the implementation
    expect(cap).toBe('50');

    // There should be bars created by initViz
    const barCount = await viz.getBarsCount();
    expect(barCount).toBeGreaterThan(0);

    // No bars should be selected initially
    const selectedCount = await viz.getSelectedCount();
    expect(selectedCount).toBe(0);

    // Step button should be enabled in initial state
    expect(await viz.isStepDisabled()).toBe(false);

    // Verify that the visual ordering matches the sorted (descending) greedy order by label sequence
    // Expected labels sorted by descending weights (from the provided implementation): B, F, G, D, A, C, E
    const labels = await viz.getAllLabels();
    expect(labels).toEqual(['B', 'F', 'G', 'D', 'A', 'C', 'E']);

    // Assert that no console errors or page errors occurred during load/initialization
    expect(consoleErrors, 'No console.error messages expected on page load').toEqual([]);
    expect(pageErrors, 'No uncaught page errors expected on page load').toEqual([]);
  });

  test('Single Step: perform one greedy selection step (S0_Initialized -> S1_StepPerformed)', async ({ page }) => {
    const viz = new VizPage(page);
    await viz.goto();

    // Comment: This validates the step() function picks the largest-fitting item (first in sorted list).
    await viz.clickStep();

    // After one step, exactly one bar should have .selected class
    await page.waitForSelector('#viz .bar.selected', { timeout: 2000 });
    const selCount = await viz.getSelectedCount();
    expect(selCount).toBe(1);

    // The selected label should be the largest item 'B' and capacity should be reduced accordingly to 25
    const selectedLabels = await viz.getSelectedLabels();
    expect(selectedLabels[0]).toBe('B');

    const capAfter = await viz.getCapacityText();
    expect(capAfter).toBe('25');

    // Step button should still be enabled because more items can fit after first selection
    expect(await viz.isStepDisabled()).toBe(false);

    // No runtime errors observed
    expect(consoleErrors).toEqual([]);
    expect(pageErrors).toEqual([]);
  });

  test('Multiple Steps lead to disabled Step button (S1_StepPerformed -> S2_StepDisabled)', async ({ page }) => {
    const viz = new VizPage(page);
    await viz.goto();

    // Perform two steps: should pick 'B' (25) then 'F' (22)
    await viz.clickStep();
    // brief wait to let DOM update
    await page.waitForTimeout(75);
    await viz.clickStep();

    // Wait for the second selection DOM update
    await page.waitForSelector('#viz .bar.selected:nth-of-type(2)', { timeout: 2000 }).catch(() => { /* ignore */ });

    // Two bars should be selected now
    const selCount = await viz.getSelectedCount();
    expect(selCount).toBe(2);

    // Selected labels should match the first two items in sorted order
    const selectedLabels = await viz.getSelectedLabels();
    expect(selectedLabels).toEqual(['B', 'F']);

    // Capacity after selecting 25 and 22 should be 3
    const capAfter = await viz.getCapacityText();
    expect(capAfter).toBe('3');

    // Now no remaining item fits into capacity 3 => stepBtn should be disabled (S2_StepDisabled)
    expect(await viz.isStepDisabled()).toBe(true);

    // Edge: Clicking step when disabled should not change selection count
    await viz.clickStep().catch(() => { /* clicking a disabled button might throw depending on environment, ignore */ });
    // small pause
    await page.waitForTimeout(50);
    expect(await viz.getSelectedCount()).toBe(2);

    // No runtime errors
    expect(consoleErrors).toEqual([]);
    expect(pageErrors).toEqual([]);
  });

  test('ResetClick resets the visualization (S2_StepDisabled -> S0_Initialized via ResetClick)', async ({ page }) => {
    const viz = new VizPage(page);
    await viz.goto();

    // Drive to disabled state by repeatedly clicking step until disabled
    // Use a loop to click until the step button becomes disabled
    while (!(await viz.isStepDisabled())) {
      await viz.clickStep();
      // small delay to let UI update
      await page.waitForTimeout(60);
    }

    // Ensure we are in disabled state
    expect(await viz.isStepDisabled()).toBe(true);
    const beforeResetSelected = await viz.getSelectedCount();
    expect(beforeResetSelected).toBeGreaterThan(0); // sanity check that something got selected

    // Now click reset
    await viz.clickReset();

    // After reset, state should be re-initialized: no selected bars, capacity = 50, step enabled
    // Wait for DOM to reflect reset (bars may be re-created)
    await page.waitForSelector('#viz .bar', { timeout: 2000 });

    const afterSelected = await viz.getSelectedCount();
    expect(afterSelected).toBe(0);

    const capAfterReset = await viz.getCapacityText();
    expect(capAfterReset).toBe('50');

    expect(await viz.isStepDisabled()).toBe(false);

    // Verify that the number of bars remains consistent with initial creation (7 expected)
    const barCount = await viz.getBarsCount();
    expect(barCount).toBe(7);

    // No runtime errors
    expect(consoleErrors).toEqual([]);
    expect(pageErrors).toEqual([]);
  });

  test('Edge case: repeated rapid clicks do not select the same item twice and selection stops when no fit', async ({ page }) => {
    const viz = new VizPage(page);
    await viz.goto();

    // Rapidly click step multiple times (more than number of items) to stress selection logic
    for (let i = 0; i < 10; i++) {
      // If button is disabled, break early
      if (await viz.isStepDisabled()) break;
      await viz.clickStep();
    }

    // After rapid clicks, stepBtn should be disabled because eventually no item fits
    expect(await viz.isStepDisabled()).toBe(true);

    // Selected bars should be unique (DOM should not duplicate the same bar)
    const selectedLabels = await viz.getSelectedLabels();
    const unique = Array.from(new Set(selectedLabels));
    expect(unique.length).toBe(selectedLabels.length);

    // Ensure capacity is non-negative and equals 50 minus sum of selected weights.
    // We don't directly compute sum here (avoid duplicating app logic); ensure capacity is a string integer
    const capText = await viz.getCapacityText();
    expect(/^\d+$/.test(capText)).toBe(true);
    expect(Number(capText)).toBeGreaterThanOrEqual(0);
    expect(Number(capText)).toBeLessThanOrEqual(50);

    // No runtime errors
    expect(consoleErrors).toEqual([]);
    expect(pageErrors).toEqual([]);
  });

  test('Sanity: initViz is re-invoked on reset (onEnter action verification)', async ({ page }) => {
    const viz = new VizPage(page);
    await viz.goto();

    // The entry action initViz() is called at page load and on reset.
    // Validate that after reset the DOM contains freshly created bars (labels in expected order) and capacity restored.
    const initialLabels = await viz.getAllLabels();
    expect(initialLabels).toEqual(['B', 'F', 'G', 'D', 'A', 'C', 'E']);
    await viz.clickStep();
    await page.waitForTimeout(50);
    expect(await viz.getSelectedCount()).toBe(1);

    // Reset and verify initViz effect again
    await viz.clickReset();
    await page.waitForSelector('#viz .bar', { timeout: 2000 });

    const labelsAfterReset = await viz.getAllLabels();
    expect(labelsAfterReset).toEqual(['B', 'F', 'G', 'D', 'A', 'C', 'E']);
    expect(await viz.getSelectedCount()).toBe(0);
    expect(await viz.getCapacityText()).toBe('50');

    // No runtime errors
    expect(consoleErrors).toEqual([]);
    expect(pageErrors).toEqual([]);
  });
});