import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/f5b11fc1-fa7c-11f0-adc7-178f556b1ee0.html';

// Page Object for the DFS demo page
class DfsPage {
  /**
   * @param {import('@playwright/test').Page} page
   */
  constructor(page) {
    this.page = page;
    this.buttonSelector = '#dfs-button';
  }

  async goto() {
    await this.page.goto(APP_URL);
  }

  async clickPerformDfs() {
    await this.page.click(this.buttonSelector);
  }

  async isButtonVisible() {
    return await this.page.isVisible(this.buttonSelector);
  }
}

test.describe('DFS FSM - states and transitions (f5b11fc1...ee0)', () => {
  // Shared variables for console and page errors captured for each test
  let consoleMessages;
  let pageErrors;
  let dfsPage;

  test.beforeEach(async ({ page }) => {
    consoleMessages = [];
    pageErrors = [];

    // Capture console messages emitted by the page (logs like "Starting ..." and "Visiting ...")
    page.on('console', (msg) => {
      // Record type (log, error, warning, etc.) and text for assertions
      try {
        consoleMessages.push({ type: msg.type(), text: msg.text() });
      } catch (e) {
        // Some console messages can throw when reading, but we still want tests to continue
        consoleMessages.push({ type: 'unknown', text: String(e) });
      }
    });

    // Capture uncaught page errors (ReferenceError, TypeError, etc.)
    page.on('pageerror', (err) => {
      pageErrors.push(err);
    });

    dfsPage = new DfsPage(page);
    await dfsPage.goto();

    // Allow a small delay to ensure initial script-run logs and any automatic clicks are processed.
    await dfsPage.page.waitForTimeout(100);
  });

  test.afterEach(async ({ page }) => {
    // Give a brief pause to ensure any late console events are captured before the test completes.
    await page.waitForTimeout(20);
  });

  test('Initial state entry: "Starting DFS traversal from A" is logged on load', async () => {
    // Validate that the button exists and is visible
    expect(await dfsPage.isButtonVisible()).toBeTruthy();

    // Check that the page logged the initial entry action for S0_Initial
    const startingMsg = consoleMessages.find((m) => /Starting DFS traversal from A/.test(m.text));
    expect(startingMsg).toBeTruthy();

    // Ensure the starting log appears as a console "log" type
    expect(startingMsg.type).toBe('log');
  });

  test('Transition S0_Initial -> S1_Visiting_Node occurs on initial automatic click: "Visiting A" is logged and ReferenceError for missing graph occurs', async () => {
    // The script automatically triggers a click after logging the starting message.
    // Check that "Visiting A" was emitted.
    const visitingMsgIndex = consoleMessages.findIndex((m) => /Visiting A/.test(m.text));
    expect(visitingMsgIndex).toBeGreaterThanOrEqual(0);

    // There should be at least one page error generated by the handler attempting to access `graph`
    expect(pageErrors.length).toBeGreaterThanOrEqual(1);

    // The first page error should be a ReferenceError mentioning 'graph' (graph is not defined)
    const firstError = pageErrors[0];
    // The page error object message should contain 'graph' and typically 'is not defined'
    expect(String(firstError.message || firstError)).toMatch(/graph/i);
    expect(String(firstError.message || firstError)).toMatch(/not defined/i);

    // Confirm that the "Visiting A" log appears before the ReferenceError was captured (order sanity check)
    // We can't strictly guarantee exact timestamp order, but Visiting should exist in console messages.
    const visitingMsg = consoleMessages.find((m) => /Visiting A/.test(m.text));
    expect(visitingMsg).toBeTruthy();
  });

  test('Transition S1_Visiting_Node -> S2_Already_Visited on subsequent click: "Already visited A" is logged and no new ReferenceError occurs', async ({ page }) => {
    // Capture current number of page errors before the manual click
    const errorsBefore = pageErrors.length;

    // Perform a manual click to simulate the transition that should hit the "else" branch
    await dfsPage.clickPerformDfs();

    // Wait briefly for handler to run and logs to flush
    await page.waitForTimeout(50);

    // Find "Already visited A" in console messages
    const alreadyVisited = consoleMessages.find((m) => /Already visited A/.test(m.text));
    expect(alreadyVisited).toBeTruthy();
    expect(alreadyVisited.type).toBe('log');

    // Ensure no new page errors were introduced by this click (i.e., the problematic graph access path wasn't hit)
    expect(pageErrors.length).toBe(errorsBefore);
  });

  test('Repeated clicks remain in Already Visited state and do not introduce additional ReferenceErrors', async ({ page }) => {
    const initialErrors = pageErrors.length;

    // Click multiple times and ensure the page logs "Already visited A" repeatedly
    const clickCount = 3;
    for (let i = 0; i < clickCount; i++) {
      await dfsPage.clickPerformDfs();
      await page.waitForTimeout(30);
    }

    // Count how many "Already visited A" messages exist in the console log
    const alreadyVisits = consoleMessages.filter((m) => /Already visited A/.test(m.text));
    expect(alreadyVisits.length).toBeGreaterThanOrEqual(clickCount); // at least as many as we clicked

    // Ensure no new page errors were created by the repeated clicks
    expect(pageErrors.length).toBe(initialErrors);
  });

  test('Edge case: Attempting to read local variables declared with let from page.evaluate throws ReferenceError', async () => {
    // The page script declares `let currentNode = 'A';` at top-level (not attached to window).
    // Attempting to access currentNode directly from page.evaluate should cause a ReferenceError.
    // We assert that the evaluation rejects with an error mentioning currentNode is not defined.

    // Use Playwright's expect(...).rejects to assert the promise rejects.
    await expect(dfsPage.page.evaluate(() => currentNode)).rejects.toThrow(/currentNode is not defined/);
  });

  test('Observability: console messages include sequence of S0 -> S1 and then S2 logs as interactions proceed', async ({ page }) => {
    // At this point, initial automatic click has already happened (S0 -> S1 attempted).
    // Perform an additional click to ensure S2 "Already visited" appears (if not already).
    await dfsPage.clickPerformDfs();
    await page.waitForTimeout(50);

    // Check that we have observed logs for all FSM states' entry actions:
    const s0 = consoleMessages.find((m) => /Starting DFS traversal from A/.test(m.text));
    const s1 = consoleMessages.find((m) => /Visiting A/.test(m.text));
    const s2 = consoleMessages.find((m) => /Already visited A/.test(m.text));

    expect(s0).toBeTruthy();
    expect(s1).toBeTruthy();
    expect(s2).toBeTruthy();
  });

  test('Error scenario: ensure the ReferenceError details appear in pageerror stack/message for debugging', async () => {
    // There should be at least one captured page error from the initial run.
    expect(pageErrors.length).toBeGreaterThanOrEqual(1);

    const err = pageErrors[0];
    const msg = String(err.message || err);
    // The message and/or stack should include "ReferenceError" and "graph"
    expect(msg).toMatch(/ReferenceError/i);
    expect(msg).toMatch(/graph/i);
  });
});