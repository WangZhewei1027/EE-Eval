import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/0441bfd3-fa79-11f0-8a8e-bbe4f11717c6.html';

test.describe('B-Tree Interactive Application - App ID 0441bfd3-fa79-11f0-8a8e-bbe4f11717c6', () => {
  // Arrays to capture runtime console messages and page errors for assertions
  let consoleMessages = [];
  let pageErrors = [];

  // References to listeners so we can remove them in teardown
  let consoleListener;
  let pageErrorListener;

  test.beforeEach(async ({ page }) => {
    // reset arrays
    consoleMessages = [];
    pageErrors = [];

    // attach listeners to capture console messages and page errors
    consoleListener = (msg) => {
      try {
        // store the console message text and type
        consoleMessages.push({ type: msg.type(), text: msg.text() });
      } catch (e) {
        // Defensive: if retrieving text throws, capture the event type only
        consoleMessages.push({ type: 'unknown', text: String(msg) });
      }
    };
    pageErrorListener = (err) => {
      pageErrors.push(err);
    };

    page.on('console', consoleListener);
    page.on('pageerror', pageErrorListener);

    // Navigate to the application page exactly as-is
    await page.goto(APP_URL);
  });

  test.afterEach(async ({ page }) => {
    // cleanup listeners
    if (consoleListener) page.off('console', consoleListener);
    if (pageErrorListener) page.off('pageerror', pageErrorListener);

    // small pause to allow any late errors to surface (if running in a noisy environment)
    await new Promise((res) => setTimeout(res, 50));
  });

  test('S0_Idle: Initial UI renders with expected elements (Idle state)', async ({ page }) => {
    // This test validates the Idle state (S0_Idle) UI elements and initial DOM content.
    // It asserts the presence of the Generate button and the Node Properties placeholders.

    // The "Generate B-Tree" button should be present and visible.
    const generateBtn = page.locator('#generate-bt');
    await expect(generateBtn).toBeVisible();
    await expect(generateBtn).toHaveText('Generate B-Tree');

    // The Node Properties placeholders should exist and contain the default text from HTML.
    const keySpan = page.locator('#key');
    const valueSpan = page.locator('#value');
    await expect(keySpan).toHaveText('Key');
    await expect(valueSpan).toHaveText('Value');

    // There should be no page errors immediately upon page load in a healthy environment.
    // However, we capture any that appear so later tests can reason about them.
    expect(Array.isArray(pageErrors)).toBeTruthy();
  });

  test('Entry action renderPage() is missing -> calling it causes ReferenceError (onEnter verification)', async ({ page }) => {
    // FSM mentions renderPage() as an entry action for S0_Idle.
    // The implementation does NOT define renderPage; calling it should result in a ReferenceError.
    // We intentionally call it to observe the natural ReferenceError (no patching or fixing).

    const result = await page.evaluate(() => {
      try {
        // Attempt to call the non-existent renderPage function.
        // We return the caught error's name & message instead of letting it crash the test runner.
        renderPage();
        return { success: true };
      } catch (e) {
        return { success: false, name: e && e.name, message: e && e.message };
      }
    });

    // Verify that calling renderPage() failed and produced a ReferenceError relating to renderPage.
    expect(result.success).toBe(false);
    expect(result.name).toBe('ReferenceError');
    expect(String(result.message)).toMatch(/renderPage/);
  });

  test('GenerateBTree event (click #generate-bt) triggers TypeError due to bt.generate missing (transition verification)', async ({ page }) => {
    // This test validates the FSM transition from S0_Idle -> S1_TreeGenerated by simulating the user click.
    // The page's click handler calls bt.generate(), but the implementation does not define bt.generate,
    // so a TypeError is expected. We use page.waitForEvent to capture the uncaught page error.

    // Wait for a pageerror event as a result of clicking the button.
    const [errorEvent] = await Promise.all([
      page.waitForEvent('pageerror'), // wait for the uncaught exception
      page.click('#generate-bt'), // trigger the event that causes the error
    ]);

    // pageerror event yields an Error object; ensure it indicates a missing function on bt.
    // Messages may vary across browsers; we assert on common substrings.
    const errMessage = errorEvent && errorEvent.message ? errorEvent.message : String(errorEvent);
    expect(errMessage.length).toBeGreaterThan(0);
    // It should mention that bt.generate is not a function or similar "not a function" wording.
    expect(
      errMessage.includes('bt.generate') || errMessage.includes('not a function') || errMessage.includes('is not a function')
    ).toBeTruthy();

    // Also ensure our pageErrors array captured the error via the listener
    expect(pageErrors.length).toBeGreaterThanOrEqual(1);
    // Confirm the most recent captured error message matches the awaited event
    const captured = pageErrors[pageErrors.length - 1];
    expect(String(captured.message || captured)).toContain('not a function');
  });

  test('Direct generate() invocation (alternative entry action) successfully builds tree via generateTree', async ({ page }) => {
    // The FSM indicates generate() as an entry action for S1_TreeGenerated.
    // While the click handler calls bt.generate() (which is missing), there's also a global generate() function
    // in the implementation that calls bt.generateTree(...). We invoke that directly to validate that
    // the underlying generateTree implementation produces a bt.root structure as expected.

    const rootSnapshot = await page.evaluate(() => {
      try {
        // Call the global generate() which delegates to bt.generateTree(...)
        generate();

        // Inspect bt.root structure and its basic properties
        function serialize(node) {
          if (!node) return null;
          return {
            key: node.key === undefined ? null : node.key,
            value: node.value === undefined ? null : node.value,
            leftExists: !!node.left,
            rightExists: !!node.right,
            leftKey: node.left ? node.left.key : null,
            rightKey: node.right ? node.right.key : null,
          };
        }

        return { success: true, root: serialize(bt.root) };
      } catch (e) {
        return { success: false, name: e && e.name, message: e && e.message };
      }
    });

    // Ensure invocation succeeded and created a root node structure.
    expect(rootSnapshot.success).toBe(true);
    expect(rootSnapshot.root).toBeTruthy();
    // Given the call generateTree(0,0,0,10) in the implementation, mid should be 0.
    expect(rootSnapshot.root.key).toBe(0);
    expect(rootSnapshot.root.value).toBe(0);
    // The left child was created with start and mid = 0
    expect(rootSnapshot.root.leftExists).toBe(true);
    expect(rootSnapshot.root.leftKey).toBe(0);
    // Right child was constructed as mid+1 which becomes 1 (even if end was 0)
    expect(rootSnapshot.root.rightExists).toBe(true);
    expect(rootSnapshot.root.rightKey).toBe(1);
  });

  test('Edge case: multiple clicks always produce a TypeError each time (robustness)', async ({ page }) => {
    // Clicking the button multiple times should repeatedly attempt to call bt.generate()
    // and thus repeatedly produce uncaught TypeError events. We assert that multiple errors occur.

    // Perform two clicks and capture two pageerror events.
    const errorEvents = [];
    // Start two promises to wait for pageerror events
    const waitErr1 = page.waitForEvent('pageerror').then(e => { errorEvents.push(e); return e; });
    await page.click('#generate-bt'); // first click triggers first error
    const ev1 = await waitErr1;

    const waitErr2 = page.waitForEvent('pageerror').then(e => { errorEvents.push(e); return e; });
    await page.click('#generate-bt'); // second click triggers second error
    const ev2 = await waitErr2;

    // Both errors should be TypeError related to bt.generate
    expect(errorEvents.length).toBeGreaterThanOrEqual(2);
    for (const ev of errorEvents) {
      const m = ev && ev.message ? ev.message : String(ev);
      expect(m).toMatch(/function/i); // contains word function (e.g., "is not a function")
    }

    // Our pageErrors listener should have registered at least two errors as well
    expect(pageErrors.length).toBeGreaterThanOrEqual(2);
  });

  test('Sanity check: console output captured and accessible (observability)', async ({ page }) => {
    // This test demonstrates that console messages are observed by the test harness.
    // The implementation does not log intentionally, but we assert that the consoleMessages array exists
    // and is an array (if there are messages, we inspect them). This ensures observability is active.

    // No new actions, just check the captured messages array
    expect(Array.isArray(consoleMessages)).toBe(true);

    // If any console messages were captured, ensure they have the expected shape
    if (consoleMessages.length > 0) {
      for (const msg of consoleMessages) {
        expect(msg).toHaveProperty('type');
        expect(msg).toHaveProperty('text');
      }
    }
  });
});