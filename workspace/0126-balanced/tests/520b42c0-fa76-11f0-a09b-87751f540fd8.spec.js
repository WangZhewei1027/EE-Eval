import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-balanced/html/520b42c0-fa76-11f0-a09b-87751f540fd8.html';

test.describe('Refactoring Example (FSM: Idle -> DataUpdated)', () => {
  // Navigate to the page fresh for each test to avoid cross-test pollution.
  test.beforeEach(async ({ page }) => {
    await page.goto(APP_URL);
  });

  test.afterEach(async ({ page }) => {
    // give any pending pageerror/console handlers time to flush
    await page.waitForTimeout(50);
    // remove any listeners if set by tests (none persisted across tests here)
  });

  test('S0_Idle: initial Idle state - button exists and updateData function is defined', async ({ page }) => {
    // Validate the "Update Data" button is present and has the expected onclick attribute.
    const updateButton = page.locator('button[onclick="updateData()"]');
    await expect(updateButton).toHaveCount(1);
    await expect(updateButton).toBeVisible();
    await expect(updateButton).toHaveText('Update Data');

    // The script defining updateData should be present as part of the page.
    // Verify the page source contains the evidence line from the FSM entry action.
    const pageHtml = await page.content();
    await expect(pageHtml).toContain('document.getElementById("user-data").innerHTML = JSON.stringify(data);');

    // Confirm that the global function updateData exists (this does not modify the app).
    const typeOfUpdateData = await page.evaluate(() => typeof updateData);
    expect(typeOfUpdateData).toBe('function');

    // Verify there is no element with id "user-data" present initially (this is why the runtime will fail later).
    const userDataElement = await page.$('#user-data');
    expect(userDataElement).toBeNull();
  });

  test('UpdateData event: clicking the button attempts transition to DataUpdated and triggers runtime error', async ({ page }) => {
    // Listen for a pageerror which is expected because the DOM lacks #user-data and updateData tries to set innerHTML.
    const errorPromise = page.waitForEvent('pageerror');

    // Click the button to fire the UpdateData event.
    await page.click('button[onclick="updateData()"]');

    // Await the runtime error generated by the failing updateData handler.
    const pageError = await errorPromise;

    // The runtime error message may vary across engines; ensure it references innerHTML/null/cannot set.
    expect(pageError).toBeTruthy();
    expect(typeof pageError.message).toBe('string');
    // Match common message patterns around trying to set innerHTML on null.
    expect(pageError.message).toMatch(/innerHTML|Cannot set properties of null|Cannot set property|null/gi);

    // Ensure the DOM still does not contain a user-data element (transition did not successfully place JSON into DOM).
    const userDataElementAfter = await page.$('#user-data');
    expect(userDataElementAfter).toBeNull();
  });

  test('Edge case: multiple UpdateData clicks produce repeated runtime errors', async ({ page }) => {
    // Prepare to capture multiple pageerror events.
    const errors = [];
    const handler = (err) => errors.push(err);
    page.on('pageerror', handler);

    // Click the button three times in succession, each should trigger the failing code path.
    await page.click('button[onclick="updateData()"]');
    // small delay to allow error to be raised and captured
    await page.waitForTimeout(50);
    await page.click('button[onclick="updateData()"]');
    await page.waitForTimeout(50);
    await page.click('button[onclick="updateData()"]');
    // wait to collect errors
    await page.waitForTimeout(200);

    // We expect at least 1 error; due to timing, ensure we got multiple errors.
    expect(errors.length).toBeGreaterThanOrEqual(1);

    // Each captured error should indicate the same general issue (attempt to set innerHTML on a missing element).
    for (const e of errors) {
      expect(e).toBeTruthy();
      expect(typeof e.message).toBe('string');
      expect(e.message).toMatch(/innerHTML|Cannot set properties of null|Cannot set property|null/gi);
    }

    page.removeListener('pageerror', handler);
  });

  test('Console observation: runtime error from UpdateData is observable via pageerror and may surface as console errors', async ({ page }) => {
    // Collect console messages during the interaction.
    const consoleMessages = [];
    const consoleHandler = msg => {
      // capture message text and type for assertions
      consoleMessages.push({ text: msg.text(), type: msg.type() });
    };
    page.on('console', consoleHandler);

    // Wait for pageerror and click
    const [pageError] = await Promise.all([
      page.waitForEvent('pageerror'),
      page.click('button[onclick="updateData()"]')
    ]);

    // Give console a moment to flush any messages
    await page.waitForTimeout(100);

    // pageError should exist and reflect the failing DOM access.
    expect(pageError).toBeTruthy();
    expect(pageError.message).toMatch(/innerHTML|Cannot set properties of null|Cannot set property|null/gi);

    // It's acceptable if there are zero console 'error' messages since runtime errors surface as pageerror events,
    // but if console error messages exist, they should reference the runtime issue.
    const errorConsoleMsgs = consoleMessages.filter(m => m.type === 'error' || /error/i.test(m.type));
    if (errorConsoleMsgs.length > 0) {
      // Ensure at least one console error references the problematic DOM operation
      const joined = errorConsoleMsgs.map(m => m.text).join(' | ');
      expect(joined).toMatch(/innerHTML|Cannot set properties of null|Cannot set property|null/gi);
    }

    page.removeListener('console', consoleHandler);
  });

  test('FSM evidence validation: page source contains pre-refactoring and after-refactoring code snippets as expected', async ({ page }) => {
    // Validate that the page contains the "Before Refactoring" data snippet and the "After Refactoring" snippet.
    const content = await page.content();
    await expect(content).toContain('Before Refactoring:');
    await expect(content).toContain('After Refactoring:');
    // Also validate that the specific var data block is present (evidence of expected implementation).
    await expect(content).toContain('"users": [');
    await expect(content).toContain('"name": "John Doe"');
  });
});