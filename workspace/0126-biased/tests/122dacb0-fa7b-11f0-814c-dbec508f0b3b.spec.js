import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/122dacb0-fa7b-11f0-814c-dbec508f0b3b.html';

// Page Object for interactions with the form page
class FormPage {
  /**
   * @param {import('@playwright/test').Page} page
   */
  constructor(page) {
    this.page = page;
    this.nameInput = page.locator('#name');
    this.ageInput = page.locator('#age');
    this.form = page.locator('#test-form');
    this.submitButton = page.locator('button[type="submit"]');
  }

  async goto() {
    await this.page.goto(APP_URL, { waitUntil: 'load' });
  }

  async fillName(name) {
    await this.nameInput.fill(name);
  }

  // fillAge will trigger the input event listeners on the page
  async fillAge(age) {
    await this.ageInput.fill(String(age));
  }

  async submit() {
    await this.submitButton.click();
  }
}

test.describe('Interactive application - FSM validation (122dacb0-fa7b-11f0-814c-dbec508f0b3b)', () => {
  let formPage;
  let consoleMessages;
  let pageErrors;

  // Setup before each test: prepare arrays to capture console and page errors,
  // attach listeners, and navigate to the page.
  test.beforeEach(async ({ page }) => {
    consoleMessages = [];
    pageErrors = [];

    // Capture console messages for assertions (e.g. form submit log)
    page.on('console', (msg) => {
      // Store the text; helps assert console.log entry actions
      consoleMessages.push(msg.text());
    });

    // Capture page errors (runtime exceptions). Per requirements we must
    // observe them and assert they occur naturally.
    page.on('pageerror', (err) => {
      pageErrors.push(err);
    });

    formPage = new FormPage(page);
    await formPage.goto();
  });

  // Test that the page initially renders the expected form elements (S0_Idle)
  test('S0_Idle: page renders form, inputs and submit button', async ({ page }) => {
    // Verify form exists
    await expect(page.locator('#test-form')).toBeVisible();

    // Verify name and age inputs exist and are required
    const name = page.locator('#name');
    const age = page.locator('#age');
    await expect(name).toBeVisible();
    await expect(age).toBeVisible();

    // Check 'required' attribute presence
    await expect(name).toHaveAttribute('required', 'true');
    await expect(age).toHaveAttribute('required', 'true');

    // Verify the submit button is present
    await expect(page.locator('button[type="submit"]')).toBeVisible();

    // The page includes a bug in the inline script that attempts to attach an event
    // listener to an element with id="submit" which doesn't exist. That should
    // produce a runtime error; assert at least one page error was recorded.
    // It's valid for this assertion to be true across environments where the error occurs.
    expect(pageErrors.length).toBeGreaterThanOrEqual(1);
    // Check that at least one error message mentions the missing element or addEventListener
    const joined = pageErrors.map(e => String(e)).join(' | ');
    expect(joined.toLowerCase()).toMatch(/null|cannot read properties|addEventListener/);
  });

  // Test the AGE_INPUT event when age < 18 -> S2_AgeValidation
  test('S2_AgeValidation: entering age below 18 triggers underage alert', async ({ page }) => {
    // Prepare to catch the dialog generated by the input event
    const dialogPromise = page.waitForEvent('dialog');

    // Enter age that is less than 18
    await formPage.fillAge(16);

    // Wait for dialog and assert text
    const dialog = await dialogPromise;
    expect(dialog.message()).toBe('You must be at least 18 years old to submit your application.');
    await dialog.accept();
  });

  // Test transition from S2_AgeValidation to S0_Idle (via AGE_INPUT) when changing to eligible age
  test('Transition S2 -> S0: updating age from <18 to >=18 triggers eligible alert', async ({ page }) => {
    // First enter a below-18 value and accept the dialog
    const firstDialogPromise = page.waitForEvent('dialog');
    await formPage.fillAge(17);
    const firstDialog = await firstDialogPromise;
    expect(firstDialog.message()).toBe('You must be at least 18 years old to submit your application.');
    await firstDialog.accept();

    // Now change the age to an eligible value and catch the new dialog
    const secondDialogPromise = page.waitForEvent('dialog');
    await formPage.fillAge(18);
    const secondDialog = await secondDialogPromise;
    expect(secondDialog.message()).toBe('You are eligible to submit your application.');
    await secondDialog.accept();
  });

  // Test S3_AgeEligible: entering eligible age triggers eligible alert
  test('S3_AgeEligible: entering age >= 18 triggers eligible alert', async ({ page }) => {
    const dialogPromise = page.waitForEvent('dialog');
    await formPage.fillAge(21);
    const dialog = await dialogPromise;
    expect(dialog.message()).toBe('You are eligible to submit your application.');
    await dialog.accept();
  });

  // Test S1_FormSubmitted: submitting the form logs Name and Age to the console
  test('S1_FormSubmitted: submitting the form emits console.log with Name and Age', async ({ page }) => {
    // Fill name first
    await formPage.fillName('Alice');

    // When we fill the age, the page's input handler will show an alert.
    // We must handle that dialog to proceed to submit.
    const ageDialogPromise = page.waitForEvent('dialog');
    await formPage.fillAge(30);
    const ageDialog = await ageDialogPromise;
    // This should be the eligible alert for >=18
    expect(ageDialog.message()).toBe('You are eligible to submit your application.');
    await ageDialog.accept();

    // Now prepare to capture a console.log entry that contains the Name and Age string
    // Use waitForEvent with a predicate to target the correct console message.
    const consolePromise = page.waitForEvent('console', {
      predicate: (msg) => msg.text().includes('Name:'),
    });

    // Submit the form; the form's submit listener prevents default and logs the name and age
    await formPage.submit();

    // Await the targeted console message and assert its content
    const consoleMsg = await consolePromise;
    const text = consoleMsg.text();
    expect(text).toContain('Name: Alice, Age: 30');
  });

  // Edge case: submitting with empty age should still invoke the submit handler
  // and log Age: NaN (parseInt of empty string -> NaN)
  test('Edge case: submit with empty age logs Age: NaN', async ({ page }) => {
    // Fill name but leave age empty
    await formPage.fillName('Bob');

    // Ensure age field is cleared; filling empty string triggers input event with NaN,
    // which may produce an alert (age < 18 check with NaN is falsey and comparisons behave).
    // To be safe, clear and handle any potential dialog generated when clearing the field.
    // Start waiting for a possible dialog, but with short timeout handling.
    const possibleDialogPromise = page.waitForEvent('dialog', { timeout: 500 }).catch(() => null);
    await formPage.fillAge(''); // clear
    const possibleDialog = await possibleDialogPromise;
    if (possibleDialog) {
      // If a dialog appeared, accept it to proceed
      await possibleDialog.accept();
    }

    // Now submit and capture console output
    const consolePromise = page.waitForEvent('console', {
      predicate: (msg) => msg.text().includes('Name:'),
    });

    await formPage.submit();

    const consoleMsg = await consolePromise;
    const text = consoleMsg.text();
    // Age may be "NaN" in the console output because parseInt('') === NaN
    expect(text).toContain('Name: Bob, Age:');
    // Check that Age portion contains NaN or empty; we expect NaN per implementation
    expect(/Age:\s*NaN/.test(text)).toBe(true);
  });

  // Verify that the page runtime error (the bug in the inline script) is observed
  // and that it is a TypeError related to attempting to addEventListener on null.
  test('Runtime error observed on load: missing #submit element causes TypeError', async ({ page }) => {
    // At least one page error should have been captured during page load
    expect(pageErrors.length).toBeGreaterThanOrEqual(1);

    // Find a page error that looks like a TypeError about null or addEventListener
    const matches = pageErrors.filter((e) => {
      const text = String(e).toLowerCase();
      return text.includes('typeerror') || text.includes('cannot read') || text.includes('null');
    });

    expect(matches.length).toBeGreaterThanOrEqual(1);

    // Make an explicit assertion on the message content for clarity in test reports
    const joined = matches.map(e => String(e)).join(' | ');
    expect(joined.toLowerCase()).toMatch(/cannot read|null|typeerror|addeventlistener/);
  });
});