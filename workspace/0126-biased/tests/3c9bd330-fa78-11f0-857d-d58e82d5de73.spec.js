import { test, expect } from '@playwright/test';

const APP_URL = 'http://127.0.0.1:5500/workspace/0126-biased/html/3c9bd330-fa78-11f0-857d-d58e82d5de73.html';

// Page Object for the Logistic Regression visual page
class LogisticPage {
  /**
   * @param {import('@playwright/test').Page} page
   */
  constructor(page) {
    this.page = page;
    this.selectors = {
      plot: '#plot',
      logisticCurve: '#logisticCurve',
      dataPointsGroup: '#dataPoints',
      togglePointsBtn: '#togglePointsBtn',
      resetCurveBtn: '#resetCurveBtn'
    };
  }

  async goto() {
    // Wait for full load so that window 'load' handlers run
    await this.page.goto(APP_URL, { waitUntil: 'load' });
  }

  async getToggleButton() {
    return this.page.locator(this.selectors.togglePointsBtn);
  }

  async getResetButton() {
    return this.page.locator(this.selectors.resetCurveBtn);
  }

  async getDataPoints() {
    return this.page.locator(`${this.selectors.dataPointsGroup} > circle`);
  }

  async getDataPointsCount() {
    return this.getDataPoints().count();
  }

  async getFirstPointOpacity() {
    const first = this.page.locator(`${this.selectors.dataPointsGroup} > circle`).first();
    // Read inline style.opacity which is set in JS
    return first.evaluate((el) => el.style.opacity || window.getComputedStyle(el).opacity);
  }

  async getToggleButtonText() {
    return this.getToggleButton().innerText();
  }

  async getToggleButtonAriaPressed() {
    return this.getToggleButton().getAttribute('aria-pressed');
  }

  async clickToggle() {
    await this.getToggleButton().click();
  }

  async clickReset() {
    await this.getResetButton().click();
  }

  async getLogisticD() {
    return this.page.locator(this.selectors.logisticCurve).getAttribute('d');
  }

  async getLogisticInlineStrokeWidth() {
    // returns the inline style strokeWidth set by animateCurvePulse() if present
    return this.page.locator(this.selectors.logisticCurve).evaluate(el => el.style.strokeWidth || '');
  }
}

test.describe('Logistic Regression — Visual Concept (FSM + UI)', () => {
  // Collect console errors and page errors for assertions
  let consoleErrors = [];
  let pageErrors = [];

  test.beforeEach(async ({ page }) => {
    consoleErrors = [];
    pageErrors = [];

    // Capture console messages of type 'error'
    page.on('console', (msg) => {
      try {
        if (msg.type() === 'error') {
          consoleErrors.push({
            text: msg.text(),
            location: msg.location()
          });
        }
      } catch (e) {
        // keep recording robustly
        consoleErrors.push({ text: `(could not extract console message)`, raw: msg });
      }
    });

    // Capture uncaught exceptions on the page
    page.on('pageerror', (err) => {
      pageErrors.push(err);
    });
  });

  test.afterEach(async () => {
    // After each test ensure no console errors or page errors were emitted during the test
    // This asserts the application did not throw runtime errors during interactions.
    expect(consoleErrors, `Expected no console.error messages but found: ${JSON.stringify(consoleErrors, null, 2)}`).toHaveLength(0);
    expect(pageErrors, `Expected no page errors but found: ${JSON.stringify(pageErrors, null, 2)}`).toHaveLength(0);
  });

  test('Initial load should execute init() and render curve & data points (S0_Idle -> S1_PointsVisible expected)', async ({ page }) => {
    // Validate that on load the app initializes: data points present and logistic curve path generated.
    const lp = new LogisticPage(page);
    await lp.goto();

    // Verify main elements exist
    await expect(page.locator('#plot')).toBeVisible();
    await expect(page.locator('#togglePointsBtn')).toBeVisible();
    await expect(page.locator('#resetCurveBtn')).toBeVisible();

    // Logistic curve should have a non-empty 'd' attribute after init()
    const d = await lp.getLogisticD();
    expect(typeof d).toBe('string');
    expect(d.length).toBeGreaterThan(0);

    // Data points should be generated by default (generateDataPoints default numPoints = 80)
    const count = await lp.getDataPointsCount();
    // assert at least some points exist and expect the intended default count
    expect(count).toBeGreaterThanOrEqual(1);
    // Many implementations use 80 by default - we assert at least 50 to avoid flakiness if modified
    expect(count).toBeGreaterThanOrEqual(50);

    // Toggle button initial state per DOM: "Hide Points" and aria-pressed="true"
    const toggleText = await lp.getToggleButtonText();
    expect(toggleText.trim()).toBe('Hide Points');

    const ariaPressed = await lp.getToggleButtonAriaPressed();
    expect(ariaPressed).toBe('true');

    // Verify first data point visible opacity indicates pointsVisible === true
    const firstOpacity = await lp.getFirstPointOpacity();
    // style.opacity may be "1" as a string
    expect(['1', '']).not.toContain(firstOpacity === '0' ? '1' : '0'); // simple sanity check
    expect(Number(firstOpacity)).toBeGreaterThan(0);
  });

  test('TogglePoints button toggles data points visibility (S1_PointsVisible <-> S2_PointsHidden)', async ({ page }) => {
    // This test validates the TogglePoints event transitions and DOM updates:
    // - toggling updates button label and aria-pressed
    // - toggling updates circles inline opacity to 0 or 1
    const lp = new LogisticPage(page);
    await lp.goto();

    // Precondition: points visible
    let toggleText = await lp.getToggleButtonText();
    expect(toggleText.trim()).toBe('Hide Points');
    let aria = await lp.getToggleButtonAriaPressed();
    expect(aria).toBe('true');
    const initialOpacity = await lp.getFirstPointOpacity();
    expect(Number(initialOpacity)).toBeGreaterThan(0);

    // Click toggle once => pointsHidden (S2_PointsHidden)
    await lp.clickToggle();

    // After click: button should show "Show Points" and aria-pressed false
    toggleText = await lp.getToggleButtonText();
    expect(toggleText.trim()).toBe('Show Points');
    aria = await lp.getToggleButtonAriaPressed();
    expect(aria).toBe('false');

    // Data points should have inline opacity 0
    const opacityAfterHide = await lp.getFirstPointOpacity();
    // style opacity is set in drawDataPoints as 0 or 1
    expect(Number(opacityAfterHide)).toBe(0);

    // Click toggle again => back to pointsVisible (S1_PointsVisible)
    await lp.clickToggle();
    toggleText = await lp.getToggleButtonText();
    expect(toggleText.trim()).toBe('Hide Points');
    aria = await lp.getToggleButtonAriaPressed();
    expect(aria).toBe('true');

    const opacityAfterShow = await lp.getFirstPointOpacity();
    expect(Number(opacityAfterShow)).toBeGreaterThan(0);

    // Rapid toggling (edge case): click three times quickly and assert final parity
    // Start from visible -> click x3 => final should be hidden (odd number)
    await lp.clickToggle(); // hidden
    await lp.clickToggle(); // visible
    await lp.clickToggle(); // hidden
    toggleText = await lp.getToggleButtonText();
    expect(toggleText.trim()).toBe('Show Points');
    aria = await lp.getToggleButtonAriaPressed();
    expect(aria).toBe('false');
    const opacityFinal = await lp.getFirstPointOpacity();
    expect(Number(opacityFinal)).toBe(0);
  });

  test('ResetCurve regenerates data, redraws curve and triggers curve pulse animation (S0_Idle ResetCurve)', async ({ page }) => {
    // Validate ResetCurve event:
    // - regenerates dataPoints (count consistent)
    // - redraws logistic curve d attribute (non-empty)
    // - animateCurvePulse sets inline strokeWidth to 8 then back to 5
    const lp = new LogisticPage(page);
    await lp.goto();

    // Capture initial states
    const beforeCount = await lp.getDataPointsCount();
    const beforeD = await lp.getLogisticD();

    // Click reset
    await lp.clickReset();

    // Immediately after click, animateCurvePulse sets strokeWidth inline to '8'
    // Note: style changes are synchronous for the immediate assignment
    const strokeWidthImmediate = await lp.getLogisticInlineStrokeWidth();
    expect(strokeWidthImmediate).toBe('8');

    // Wait for the pulse to complete - animateCurvePulse uses 150ms timeout before setting back to 5
    await page.waitForTimeout(350);

    const strokeWidthAfter = await lp.getLogisticInlineStrokeWidth();
    // After timeout the code sets strokeWidth back to '5'
    expect(strokeWidthAfter).toBe('5');

    // Curve 'd' should be present and non-empty
    const afterD = await lp.getLogisticD();
    expect(typeof afterD).toBe('string');
    expect(afterD.length).toBeGreaterThan(0);

    // Data points count should remain in expected range (regenerated)
    const afterCount = await lp.getDataPointsCount();
    expect(afterCount).toBeGreaterThanOrEqual(50);
    // It is expected that generateDataPoints creates values of similar magnitude; count should be same default normally
    // But avoid flaky strict equality due to potential implementation change
    // Check that at least something changed (d or some point positions) - non-flaky check:
    expect(afterCount).toBeGreaterThanOrEqual(beforeCount >= 1 ? 1 : 0);

    // Confirm that circles now have class "data-point" and either "positive" or "negative"
    const firstCircleClass = await page.locator('#dataPoints > circle').first().getAttribute('class');
    expect(firstCircleClass).toContain('data-point');
    expect(
      firstCircleClass.includes('positive') || firstCircleClass.includes('negative')
    ).toBe(true);
  });

  test('Accessibility and attributes: buttons have aria-labels and toggle button updates aria-pressed', async ({ page }) => {
    // Validate presence of important accessibility attributes and expected behavior on interactions
    const lp = new LogisticPage(page);
    await lp.goto();

    // Buttons have aria-label attributes
    const toggleAriaLabel = await lp.getToggleButton().getAttribute('aria-label');
    const resetAriaLabel = await lp.getResetButton().getAttribute('aria-label');
    expect(typeof toggleAriaLabel).toBe('string');
    expect(toggleAriaLabel.length).toBeGreaterThan(0);
    expect(typeof resetAriaLabel).toBe('string');
    expect(resetAriaLabel.length).toBeGreaterThan(0);

    // Toggle the points and ensure aria-pressed toggles accordingly
    const initialPressed = await lp.getToggleButtonAriaPressed();
    await lp.clickToggle();
    const afterPressed = await lp.getToggleButtonAriaPressed();
    expect(afterPressed).not.toBe(initialPressed);

    // Reset button should be clickable multiple times without throwing errors (edge case)
    await lp.clickReset();
    await lp.clickReset();
    // Wait briefly to allow any animation timeouts to run
    await page.waitForTimeout(200);
  });

  test('No runtime exceptions when performing rapid interactions (edge case)', async ({ page }) => {
    // Stress test: perform a burst of interactions: toggle many times and reset multiple times quickly
    const lp = new LogisticPage(page);
    await lp.goto();

    // Rapid sequence of interactions
    for (let i = 0; i < 6; i++) {
      await lp.clickToggle();
    }

    // Perform multiple resets in quick succession
    await Promise.all([
      lp.clickReset(),
      lp.clickReset(),
      lp.clickReset()
    ]);

    // Allow timeouts/animations to settle
    await page.waitForTimeout(500);

    // Ensure after heavy interaction the core elements still exist and curve has a d attribute
    const d = await lp.getLogisticD();
    expect(typeof d).toBe('string');
    expect(d.length).toBeGreaterThan(0);

    const count = await lp.getDataPointsCount();
    expect(count).toBeGreaterThanOrEqual(1);
  });
});